// minimalSchema returns a schema with one table (two columns: id uuid PK, name text).
func minimalSchema() *model.Schema {
	return &model.Schema{
		Tables: []model.Table{{
			Name:    "users",
			Schema:  "public",
			Comment: "User accounts",
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.T("uuid"), NotNull: true},
				{Name: "name", PGType: typeinfo.T("text"), NotNull: true},
			},
			PK: []string{"id"},
		}},
	}
}

func TestPlan_SQLOutput(t *testing.T) {
	if testing.Short() {
		t.Skip("Plan() generates .sqlsplit via WASM parser")
	}
	schema := minimalSchema()
	cfg := &config.ResolvedConfig{
		Output: map[string]config.OutputConfig[config.AbsolutePath]{
			"main": {
				Format: "sql",
				Path:   "/tmp/test-plan/test.sql",
			},
		},
	}
	registry := semtype.NewRegistry()

	result, err := Plan(schema, cfg, registry, 16)
	if err != nil {
		t.Fatalf("Plan returned error: %v", err)
	}

	// Verify SQL file is present.
	sqlContent, ok := result.Files["/tmp/test-plan/test.sql"]
	if !ok {
		t.Fatal("expected /tmp/test-plan/test.sql in result.Files")
	}

	// Verify .sqlsplit companion is present.
	splitContent, ok := result.Files["/tmp/test-plan/test.sql.sqlsplit"]
	if !ok {
		t.Fatal("expected /tmp/test-plan/test.sql.sqlsplit in result.Files")
	}

	// SQL content starts with the generated header.
	if !strings.HasPrefix(string(sqlContent), "-- Generated by pgdesign") {
		t.Errorf("SQL content should start with generated header, got: %s", string(sqlContent[:80]))
	}

	// SQL content contains CREATE TABLE.
	if !strings.Contains(string(sqlContent), "CREATE TABLE") {
		t.Error("SQL content should contain CREATE TABLE")
	}

	// .sqlsplit content is decodeable.
	stmts, decErr := splitfmt.Decode(splitContent)
	if decErr != nil {
		t.Fatalf("splitfmt.Decode failed: %v", decErr)
	}
	if len(stmts) == 0 {
		t.Error("expected at least one statement from splitfmt.Decode")
	}
}

