func TestPlan_GroupFiltering(t *testing.T) {
	if testing.Short() {
		t.Skip("Plan() generates .sqlsplit via WASM parser")
	}
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "users",
				Schema:  "public",
				Comment: "User accounts",
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.T("uuid"), NotNull: true},
					{Name: "name", PGType: typeinfo.T("text"), NotNull: true},
				},
				PK: []string{"id"},
			},
			{
				Name:    "orders",
				Schema:  "public",
				Comment: "Order records",
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.T("uuid"), NotNull: true},
					{Name: "total", PGType: typeinfo.T("numeric"), NotNull: true},
				},
				PK: []string{"id"},
			},
		},
		Groups: map[string][]string{
			"core": {"users"},
		},
	}
	cfg := &config.ResolvedConfig{
		Output: map[string]config.OutputConfig[config.AbsolutePath]{
			"core_sql": {
				Format: "sql",
				Path:   "/tmp/test-plan/core.sql",
				Groups: []string{"core"},
			},
		},
	}
	registry := semtype.NewRegistry()

	result, err := Plan(schema, cfg, registry, 16)
	if err != nil {
		t.Fatalf("Plan returned error: %v", err)
	}

	sqlContent, ok := result.Files["/tmp/test-plan/core.sql"]
	if !ok {
		t.Fatal("expected /tmp/test-plan/core.sql in result.Files")
	}

	s := string(sqlContent)
	if !strings.Contains(s, "users") {
		t.Error("filtered SQL should contain 'users' table")
	}
	if strings.Contains(s, "orders") {
		t.Error("filtered SQL should NOT contain 'orders' table (not in 'core' group)")
	}
}
