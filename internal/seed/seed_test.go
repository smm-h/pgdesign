func TestGenerate_OrderAndStructure(t *testing.T) {
	schema := testSchema()
	rng := rand.New(rand.NewSource(42))
	out, _ := Generate(schema, 3, rng, nil)

	// Must start with the header comment.
	if !strings.HasPrefix(out, "-- Seed data generated by pgdesign") {
		t.Error("output does not start with expected header comment")
	}

	// Organizations must appear before users (topological order).
	orgIdx := strings.Index(out, "INSERT INTO public.organizations")
	usrIdx := strings.Index(out, "INSERT INTO public.users")
	if orgIdx < 0 {
		t.Fatal("missing INSERT INTO public.organizations")
	}
	if usrIdx < 0 {
		t.Fatal("missing INSERT INTO public.users")
	}
	if orgIdx >= usrIdx {
		t.Error("organizations INSERT must appear before users INSERT")
	}

	// Each table should have 3 value tuples.
	// Count occurrences of "(" at the start of value lines for organizations.
	orgBlock := out[orgIdx:usrIdx]
	orgRows := strings.Count(orgBlock, "  (")
	if orgRows != 3 {
		t.Errorf("expected 3 row tuples for organizations, got %d", orgRows)
	}

	usrBlock := out[usrIdx:]
	usrRows := strings.Count(usrBlock, "  (")
	if usrRows != 3 {
		t.Errorf("expected 3 row tuples for users, got %d", usrRows)
	}
}

