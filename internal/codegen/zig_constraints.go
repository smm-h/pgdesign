package codegen

import (
	"bytes"
	"fmt"
	"strings"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// ZigConstraintsGenerator generates Zig validation functions for table constraints.
type ZigConstraintsGenerator struct{}

// Generate produces a Zig file with validate_<table_name> functions for each
// table that has extractable constraints (NOT NULL, enum, CHECK, JSON schema).
func (g *ZigConstraintsGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by pgdesign -- do not edit.\n")
	buf.WriteString("// Regenerate with: pgdesign codegen --lang zig --mode constraints <schema-files>\n")
	if schema.Name != "" {
		fmt.Fprintf(&buf, "// Schema: %s\n", schema.Name)
	}

	buf.WriteString("\nconst std = @import(\"std\");\n")

	// Write ValidationError type.
	buf.WriteString(`
pub const ValidationError = struct {
    field: []const u8,
    message: []const u8,
};
`)

	// Collect tables with constraints.
	type tableWork struct {
		table model.Table
		cs    ConstraintSet
	}
	var work []tableWork
	for _, tbl := range schema.Tables {
		cs := ExtractConstraints(tbl, *schema)
		if !cs.HasConstraints() {
			continue
		}
		work = append(work, tableWork{table: tbl, cs: cs})
	}

	if len(work) == 0 {
		buf.WriteString("\n// No tables with extractable constraints.\n")
		return buf.Bytes(), nil
	}

	for _, tw := range work {
		writeZigValidator(&buf, tw.table, tw.cs)
	}

	return buf.Bytes(), nil
}

func writeZigValidator(buf *bytes.Buffer, table model.Table, cs ConstraintSet) {
	funcName := "validate_" + table.Name

	fmt.Fprintf(buf, "\n/// Validates constraint rules for the %s table.\n", table.Name)
	fmt.Fprintf(buf, "pub fn %s(row: anytype, allocator: std.mem.Allocator) !std.ArrayList(ValidationError) {\n", funcName)
	buf.WriteString("    var errors = std.ArrayList(ValidationError).init(allocator);\n")

	// NOT NULL checks.
	for _, col := range cs.NotNullFields {
		pgType := columnPGType(table, col)
		if isStringPGType(pgType) {
			fmt.Fprintf(buf, "    if (row.%s.len == 0) {\n", col)
		} else {
			fmt.Fprintf(buf, "    if (row.%s == null) {\n", col)
		}
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"must not be empty\" });\n", col)
		buf.WriteString("    }\n")
	}

	// Enum checks.
	for col, values := range cs.EnumFields {
		fmt.Fprintf(buf, "    {\n")
		fmt.Fprintf(buf, "        const valid = [_][]const u8{ ")
		for i, v := range values {
			if i > 0 {
				buf.WriteString(", ")
			}
			fmt.Fprintf(buf, "%q", v)
		}
		buf.WriteString(" };\n")
		buf.WriteString("        var found = false;\n")
		fmt.Fprintf(buf, "        for (valid) |v| {\n")
		fmt.Fprintf(buf, "            if (std.mem.eql(u8, row.%s, v)) {\n", col)
		buf.WriteString("                found = true;\n")
		buf.WriteString("                break;\n")
		buf.WriteString("            }\n")
		buf.WriteString("        }\n")
		buf.WriteString("        if (!found) {\n")
		fmt.Fprintf(buf, "            try errors.append(.{ .field = %q, .message = \"invalid enum value\" });\n", col)
		buf.WriteString("        }\n")
		buf.WriteString("    }\n")
	}

	// CHECK constraint checks.
	for col, expr := range cs.CheckExprs {
		pat := classifyCheck(expr)
		if pat == nil {
			fmt.Fprintf(buf, "    // CHECK on %s: %s (unrecognized pattern, skipped)\n", col, expr)
			continue
		}
		writeZigCheckPattern(buf, col, pat)
	}

	buf.WriteString("    return errors;\n")
	buf.WriteString("}\n")
}

func writeZigCheckPattern(buf *bytes.Buffer, col string, pat checkPattern) {
	switch p := pat.(type) {
	case *rangePattern:
		var lowOp, highOp string
		if p.LowIncl {
			lowOp = "<"
		} else {
			lowOp = "<="
		}
		if p.HighIncl {
			highOp = ">"
		} else {
			highOp = ">="
		}
		fmt.Fprintf(buf, "    if (row.%s %s %s or row.%s %s %s) {\n",
			col, lowOp, p.Low, col, highOp, p.High)
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"value out of range\" });\n", col)
		buf.WriteString("    }\n")

	case *comparisonPattern:
		failOp := invertComparisonOp(p.Op)
		fmt.Fprintf(buf, "    if (row.%s %s %s) {\n", col, failOp, p.Value)
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"failed check: %s %s\" });\n", col, p.Op, p.Value)
		buf.WriteString("    }\n")

	case *lengthPattern:
		failOp := invertComparisonOp(p.Op)
		fmt.Fprintf(buf, "    if (row.%s.len %s %d) {\n", col, failOp, p.Value)
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"length must be %s %d\" });\n", col, p.Op, p.Value)
		buf.WriteString("    }\n")

	case *likePattern:
		writeZigLikePattern(buf, col, p)
	}
}

// writeZigLikePattern emits Zig std.mem calls for LIKE patterns where possible.
// Patterns that cannot be expressed with std.mem are skipped with a comment.
func writeZigLikePattern(buf *bytes.Buffer, col string, p *likePattern) {
	pattern := p.Pattern
	isNot := strings.HasPrefix(strings.ToUpper(p.Op), "NOT")

	// Classify the LIKE pattern into startsWith, endsWith, contains, or skip.
	switch {
	case !strings.Contains(pattern, "%") && !strings.Contains(pattern, "_"):
		// Exact match (no wildcards) — use std.mem.eql.
		if isNot {
			fmt.Fprintf(buf, "    if (std.mem.eql(u8, row.%s, %q)) {\n", col, pattern)
		} else {
			fmt.Fprintf(buf, "    if (!std.mem.eql(u8, row.%s, %q)) {\n", col, pattern)
		}
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"does not match required pattern\" });\n", col)
		buf.WriteString("    }\n")

	case strings.HasPrefix(pattern, "%") && strings.HasSuffix(pattern, "%") && len(pattern) > 2:
		// %X% -> contains
		inner := pattern[1 : len(pattern)-1]
		if strings.Contains(inner, "%") || strings.Contains(inner, "_") {
			fmt.Fprintf(buf, "    // LIKE pattern '%s' not expressible without regex -- skipped\n", pattern)
			return
		}
		if isNot {
			fmt.Fprintf(buf, "    if (std.mem.indexOf(u8, row.%s, %q) != null) {\n", col, inner)
		} else {
			fmt.Fprintf(buf, "    if (std.mem.indexOf(u8, row.%s, %q) == null) {\n", col, inner)
		}
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"does not match required pattern\" });\n", col)
		buf.WriteString("    }\n")

	case strings.HasSuffix(pattern, "%") && !strings.HasPrefix(pattern, "%"):
		// X% -> startsWith
		prefix := pattern[:len(pattern)-1]
		if strings.Contains(prefix, "%") || strings.Contains(prefix, "_") {
			fmt.Fprintf(buf, "    // LIKE pattern '%s' not expressible without regex -- skipped\n", pattern)
			return
		}
		if isNot {
			fmt.Fprintf(buf, "    if (std.mem.startsWith(u8, row.%s, %q)) {\n", col, prefix)
		} else {
			fmt.Fprintf(buf, "    if (!std.mem.startsWith(u8, row.%s, %q)) {\n", col, prefix)
		}
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"does not match required pattern\" });\n", col)
		buf.WriteString("    }\n")

	case strings.HasPrefix(pattern, "%") && !strings.HasSuffix(pattern, "%"):
		// %X -> endsWith
		suffix := pattern[1:]
		if strings.Contains(suffix, "%") || strings.Contains(suffix, "_") {
			fmt.Fprintf(buf, "    // LIKE pattern '%s' not expressible without regex -- skipped\n", pattern)
			return
		}
		if isNot {
			fmt.Fprintf(buf, "    if (std.mem.endsWith(u8, row.%s, %q)) {\n", col, suffix)
		} else {
			fmt.Fprintf(buf, "    if (!std.mem.endsWith(u8, row.%s, %q)) {\n", col, suffix)
		}
		fmt.Fprintf(buf, "        try errors.append(.{ .field = %q, .message = \"does not match required pattern\" });\n", col)
		buf.WriteString("    }\n")

	default:
		// Complex LIKE pattern (multiple wildcards, underscore, etc.)
		fmt.Fprintf(buf, "    // LIKE pattern '%s' not expressible without regex -- skipped\n", pattern)
	}
}
