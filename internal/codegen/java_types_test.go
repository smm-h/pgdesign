func TestJavaTypesGenerator_MultipleTables(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "orders",
				Comment: "Orders",
				Columns: []model.Column{
					{Name: "id", PGType: "uuid", NotNull: true},
					{Name: "total", PGType: "numeric", NotNull: true},
				},
			},
			{
				Name:    "order_items",
				Comment: "Order items",
				Columns: []model.Column{
					{Name: "id", PGType: "integer", NotNull: true},
					{Name: "quantity", PGType: "smallint", NotNull: true},
				},
			},
		},
	}

	gen := &JavaTypesGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Both record declarations present.
	if !strings.Contains(result, "public record Orders(") {
		t.Error("missing Orders record declaration")
	}
	if !strings.Contains(result, "public record OrderItems(") {
		t.Error("missing OrderItems record declaration")
	}

	// Spot-check a field from the second table.
	if !containsJavaField(result, "quantity", "short") {
		t.Error("missing or incorrect quantity field (expected short)")
	}
}
