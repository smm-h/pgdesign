func TestKotlinValidatorGenerator_Generate(t *testing.T) {
	schema := &model.Schema{
		Name: "game",
		Tables: []model.Table{
			{
				Name:   "chat_messages",
				Schema: "game",
				Policies: []model.Policy{
					{
						Name:      "chat_read_all",
						Operation: "SELECT",
						Using:     "true",
					},
					{
						Name:         "chat_requires_enabled",
						Operation:    "INSERT",
						WithCheck:    "sender_id::text = current_setting('app.player_id') AND EXISTS (SELECT 1 FROM game.player_privacy_settings WHERE player_id = sender_id AND chat_enabled = true)",
						ErrorCode:    "chat_disabled",
						ErrorMessage: "Chat is disabled in your privacy settings",
					},
				},
			},
			{
				Name:   "follow",
				Schema: "game",
				Policies: []model.Policy{
					{
						Name:         "follow_requires_friends_enabled",
						Operation:    "INSERT",
						WithCheck:    "follower_id::text = current_setting('app.player_id') AND EXISTS (SELECT 1 FROM game.player_privacy_settings WHERE player_id = followed_id AND friends_enabled = true)",
						ErrorCode:    "target_friends_disabled",
						ErrorMessage: "This player has disabled friend/follow requests",
					},
				},
			},
		},
	}

	gen := &KotlinValidatorGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Header checks.
	if !strings.Contains(result, "// Generated by pgdesign -- do not edit manually.") {
		t.Error("missing header comment")
	}
	if !strings.Contains(result, "// Regenerate with: pgdesign codegen --lang kotlin <schema-files>") {
		t.Error("missing regenerate instruction in header")
	}
	if !strings.Contains(result, "import java.sql.Connection") {
		t.Error("missing Connection import")
	}
	if !strings.Contains(result, "data class PolicyResult(") {
		t.Error("missing PolicyResult data class")
	}

	// chat_requires_enabled validator.
	if !strings.Contains(result, "suspend fun checkChatRequiresEnabled(conn: Connection, sender_id: String): PolicyResult {") {
		t.Error("missing chat_requires_enabled function signature")
	}
	if !strings.Contains(result, "SELECT chat_enabled FROM game.player_privacy_settings WHERE player_id = ?") {
		t.Error("missing chat_enabled query")
	}
	if strings.Contains(result, "$1") {
		t.Error("Kotlin should use ? parameter markers, not $1")
	}
	if !strings.Contains(result, "prepareStatement") {
		t.Error("missing prepareStatement call")
	}
	if !strings.Contains(result, "setString(1,") {
		t.Error("missing setString call")
	}
	if !strings.Contains(result, "executeQuery") {
		t.Error("missing executeQuery call")
	}
	if !strings.Contains(result, `"chat_disabled"`) {
		t.Error("missing chat_disabled error code")
	}

	// follow_requires_friends_enabled validator.
	if !strings.Contains(result, "suspend fun checkFollowRequiresFriendsEnabled(conn: Connection, followed_id: String): PolicyResult {") {
		t.Error("missing follow_requires_friends_enabled function signature")
	}
	if !strings.Contains(result, "SELECT friends_enabled FROM game.player_privacy_settings WHERE player_id = ?") {
		t.Error("missing friends_enabled query")
	}
	if !strings.Contains(result, `"target_friends_disabled"`) {
		t.Error("missing target_friends_disabled error code")
	}

	// Should NOT contain the read_all policy (no error code).
	if strings.Contains(result, "checkChatReadAll") {
		t.Error("should not generate validator for read_all policy")
	}
}

