// containsGormField checks that a line exists containing the field name, Go type,
// and a gorm tag containing the specified substring.
func containsGormField(s, fieldName, goType, gormTagSubstr string) bool {
	for _, line := range strings.Split(s, "\n") {
		line = strings.TrimSpace(line)
		if strings.Contains(line, fieldName) &&
			strings.Contains(line, goType) &&
			strings.Contains(line, "gorm:") &&
			strings.Contains(line, gormTagSubstr) {
			return true
		}
	}
	return false
}

func TestGoGormGenerator_Basic(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "users",
			Comment: "Application users",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.MustParse("uuid"), NotNull: true},
				{Name: "email", PGType: typeinfo.MustParse("text"), NotNull: true},
				{Name: "score", PGType: typeinfo.MustParse("bigint"), NotNull: true},
				{Name: "is_active", PGType: typeinfo.MustParse("boolean"), NotNull: true},
				{Name: "created_at", PGType: typeinfo.MustParse("timestamptz"), NotNull: true},
			},
			Indexes: []model.Index{
				{Name: "idx_users_email", Columns: []string{"email"}, Unique: true},
			},
		}},
	}

	gen := &GoGormGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Header.
	if !strings.Contains(result, "// Code generated by pgdesign -- do not edit.") {
		t.Error("missing generated header comment")
	}

	// Package declaration.
	if !strings.Contains(result, "package schema") {
		t.Error("missing package declaration")
	}

	// Struct declaration.
	if !strings.Contains(result, "type Users struct {") {
		t.Error("missing struct declaration")
	}

	// Id field: primaryKey, not null, type:uuid.
	if !containsGormField(result, "Id", "uuid.UUID", "column:id") {
		t.Error("missing Id column:id tag")
	}
	if !containsGormField(result, "Id", "uuid.UUID", "primaryKey") {
		t.Error("missing Id primaryKey tag")
	}
	if !containsGormField(result, "Id", "uuid.UUID", "not null") {
		t.Error("missing Id not null tag")
	}
	if !containsGormField(result, "Id", "uuid.UUID", "type:uuid") {
		t.Error("missing Id type:uuid tag")
	}

	// Email field: uniqueIndex.
	if !containsGormField(result, "Email", "string", "column:email") {
		t.Error("missing Email column:email tag")
	}
	if !containsGormField(result, "Email", "string", "not null") {
		t.Error("missing Email not null tag")
	}
	if !containsGormField(result, "Email", "string", "type:text") {
		t.Error("missing Email type:text tag")
	}
	if !containsGormField(result, "Email", "string", "uniqueIndex:idx_users_email") {
		t.Error("missing Email uniqueIndex tag")
	}

	// Score field.
	if !containsGormField(result, "Score", "int64", "column:score") {
		t.Error("missing Score column:score tag")
	}
	if !containsGormField(result, "Score", "int64", "type:int8") {
		t.Error("missing Score type:int8 tag")
	}

	// IsActive field.
	if !containsGormField(result, "IsActive", "bool", "column:is_active") {
		t.Error("missing IsActive column:is_active tag")
	}
	if !containsGormField(result, "IsActive", "bool", "type:bool") {
		t.Error("missing IsActive type:bool tag")
	}

	// CreatedAt field.
	if !containsGormField(result, "CreatedAt", "time.Time", "column:created_at") {
		t.Error("missing CreatedAt column:created_at tag")
	}
	if !containsGormField(result, "CreatedAt", "time.Time", "type:timestamptz") {
		t.Error("missing CreatedAt type:timestamptz tag")
	}
}

