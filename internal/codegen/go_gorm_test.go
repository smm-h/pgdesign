func TestGoGormGenerator_Relationships(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "users",
				Comment: "Users",
				PK:      []string{"id"},
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.MustParse("uuid"), NotNull: true},
					{Name: "name", PGType: typeinfo.MustParse("text"), NotNull: true},
				},
			},
			{
				Name:    "orders",
				Comment: "Orders",
				PK:      []string{"id"},
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.MustParse("uuid"), NotNull: true},
					{Name: "user_id", PGType: typeinfo.MustParse("uuid"), NotNull: true},
					{Name: "total", PGType: typeinfo.MustParse("numeric"), NotNull: true},
				},
				FKs: []model.FK{
					{Name: "fk_orders_user", Columns: []string{"user_id"}, RefTable: "users", RefColumns: []string{"id"}},
				},
			},
		},
	}

	gen := &GoGormGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Orders struct should have BelongsTo: Users Users with foreignKey:UserId.
	if !containsGormField(result, "Users", "Users", "foreignKey:UserId") {
		t.Error("missing BelongsTo Users field on Orders struct")
	}

	// Users struct should have HasMany: Orderss []Orders with foreignKey:UserId.
	if !containsGormField(result, "Orderss", "[]Orders", "foreignKey:UserId") {
		t.Error("missing HasMany Orderss field on Users struct")
	}
}

func TestGoGormGenerator_NullableAndArray(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "items",
			Comment: "Items",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.MustParse("integer"), NotNull: true},
				{Name: "name", PGType: typeinfo.MustParse("text"), NotNull: false},
				{Name: "tags", PGType: typeinfo.MustParse("text"), NotNull: true, Array: true},
				{Name: "scores", PGType: typeinfo.MustParse("integer"), NotNull: false, Array: true},
			},
		}},
	}

	gen := &GoGormGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Nullable text -> *string, tag should NOT contain "not null".
	if !containsGormField(result, "Name", "*string", "column:name") {
		t.Error("missing Name *string field")
	}
	// Verify no "not null" in the Name line.
	for _, line := range strings.Split(result, "\n") {
		line = strings.TrimSpace(line)
		if strings.Contains(line, "Name") && strings.Contains(line, "*string") &&
			strings.Contains(line, "not null") {
			t.Error("Name field should not have 'not null' tag")
		}
	}

	// Array text -> []string, tag should contain type:text[].
	if !containsGormField(result, "Tags", "[]string", "type:text[]") {
		t.Error("missing Tags []string with type:text[] tag")
	}

	// Nullable array integer -> *[]int32, tag should contain type:int4[].
	if !containsGormField(result, "Scores", "*[]int32", "type:int4[]") {
		t.Error("missing Scores *[]int32 with type:int4[] tag")
	}
}

