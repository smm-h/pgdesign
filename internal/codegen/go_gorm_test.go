func TestGoGormGenerator_Enums(t *testing.T) {
	schema := &model.Schema{
		Enums: []model.Enum{{Name: "status", Values: []string{"active", "inactive"}}},
		Tables: []model.Table{{
			Name:    "users",
			Comment: "Users",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.MustParse("integer"), NotNull: true},
				{Name: "status", PGType: typeinfo.MustParse("status"), NotNull: true},
			},
		}},
	}

	gen := &GoGormGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Enum const should be present.
	if !strings.Contains(result, "StatusActive") {
		t.Error("missing StatusActive enum const")
	}
	if !strings.Contains(result, "StatusInactive") {
		t.Error("missing StatusInactive enum const")
	}

	// Status column maps to string (fallback for unrecognized PG type).
	if !containsGormField(result, "Status", "string", "column:status") {
		t.Error("missing Status string field")
	}
}

func TestGoGormGenerator_NonUniqueIndex(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "events",
			Comment: "Events",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.MustParse("integer"), NotNull: true},
				{Name: "created_at", PGType: typeinfo.MustParse("timestamptz"), NotNull: true},
			},
			Indexes: []model.Index{
				{Name: "idx_events_created_at", Columns: []string{"created_at"}, Unique: false},
			},
		}},
	}

	gen := &GoGormGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Should have non-unique index tag.
	if !containsGormField(result, "CreatedAt", "time.Time", "index:idx_events_created_at") {
		t.Error("missing CreatedAt index tag")
	}

	// Should NOT have uniqueIndex.
	for _, line := range strings.Split(result, "\n") {
		line = strings.TrimSpace(line)
		if strings.Contains(line, "CreatedAt") && strings.Contains(line, "uniqueIndex") {
			t.Error("CreatedAt should not have uniqueIndex tag")
		}
	}
}

