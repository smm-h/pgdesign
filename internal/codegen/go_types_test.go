// containsField checks that at least one line in s contains all three parts:
// the PascalCase field name, the Go type, and the db struct tag.
func containsField(s, fieldName, goType, dbTag string) bool {
	for _, line := range strings.Split(s, "\n") {
		line = strings.TrimSpace(line)
		if strings.Contains(line, fieldName) &&
			strings.Contains(line, goType) &&
			strings.Contains(line, "`db:\""+dbTag+"\"`") {
			return true
		}
	}
	return false
}

func TestGoTypesGenerator_Basic(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "users",
				Schema:  "public",
				Comment: "Application users",
				Columns: []model.Column{
					{Name: "id", PGType: "uuid", NotNull: true},
					{Name: "email", PGType: "text", NotNull: true},
					{Name: "score", PGType: "bigint", NotNull: true},
					{Name: "is_active", PGType: "boolean", NotNull: true},
					{Name: "created_at", PGType: "timestamptz", NotNull: true},
					{Name: "metadata", PGType: "jsonb", NotNull: true},
					{Name: "age", PGType: "integer", NotNull: true},
					{Name: "rating", PGType: "double precision", NotNull: true},
					{Name: "avatar", PGType: "bytea", NotNull: true},
					{Name: "balance", PGType: "numeric", NotNull: true},
				},
			},
		},
	}

	gen := &GoTypesGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Header.
	if !strings.Contains(result, "// Code generated by pgdesign -- do not edit.") {
		t.Error("missing generated header comment")
	}

	// Package declaration.
	if !strings.Contains(result, "package schema") {
		t.Error("missing package declaration")
	}

	// Struct comment and declaration.
	if !strings.Contains(result, "// Users represents the users table.") {
		t.Error("missing struct comment")
	}
	if !strings.Contains(result, "type Users struct {") {
		t.Error("missing struct declaration")
	}

	// Fields with correct types and tags.
	if !containsField(result, "Id", "uuid.UUID", "id") {
		t.Error("missing or incorrect Id field (expected uuid.UUID)")
	}
	if !containsField(result, "Email", "string", "email") {
		t.Error("missing or incorrect Email field (expected string)")
	}
	if !containsField(result, "Score", "int64", "score") {
		t.Error("missing or incorrect Score field (expected int64)")
	}
	if !containsField(result, "IsActive", "bool", "is_active") {
		t.Error("missing or incorrect IsActive field (expected bool)")
	}
	if !containsField(result, "CreatedAt", "time.Time", "created_at") {
		t.Error("missing or incorrect CreatedAt field (expected time.Time)")
	}
	if !containsField(result, "Metadata", "json.RawMessage", "metadata") {
		t.Error("missing or incorrect Metadata field (expected json.RawMessage)")
	}
	if !containsField(result, "Age", "int32", "age") {
		t.Error("missing or incorrect Age field (expected int32)")
	}
	if !containsField(result, "Rating", "float64", "rating") {
		t.Error("missing or incorrect Rating field (expected float64)")
	}
	if !containsField(result, "Avatar", "[]byte", "avatar") {
		t.Error("missing or incorrect Avatar field (expected []byte)")
	}
	if !containsField(result, "Balance", "string", "balance") {
		t.Error("missing or incorrect Balance field (expected string)")
	}

	// Imports.
	if !strings.Contains(result, `"time"`) {
		t.Error("missing time import")
	}
	if !strings.Contains(result, `"encoding/json"`) {
		t.Error("missing encoding/json import")
	}
	if !strings.Contains(result, `"github.com/google/uuid"`) {
		t.Error("missing uuid import")
	}
}

func TestGoTypesGenerator_NullableAndArray(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "items",
				Comment: "Test items",
				Columns: []model.Column{
					{Name: "id", PGType: "integer", NotNull: true},
					{Name: "name", PGType: "text", NotNull: false},
					{Name: "tags", PGType: "text", NotNull: true, Array: true},
					{Name: "scores", PGType: "integer", NotNull: false, Array: true},
					{Name: "status", PGType: "user_status", NotNull: true},
				},
			},
		},
	}

	gen := &GoTypesGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Not-null, not-array: plain type.
	if !containsField(result, "Id", "int32", "id") {
		t.Error("missing or incorrect Id field (expected int32)")
	}

	// Nullable: pointer.
	if !containsField(result, "Name", "*string", "name") {
		t.Error("missing or incorrect Name field (expected *string)")
	}

	// Array: slice.
	if !containsField(result, "Tags", "[]string", "tags") {
		t.Error("missing or incorrect Tags field (expected []string)")
	}

	// Nullable + array: pointer to slice.
	if !containsField(result, "Scores", "*[]int32", "scores") {
		t.Error("missing or incorrect Scores field (expected *[]int32)")
	}

	// Enum (unrecognized PG type): string.
	if !containsField(result, "Status", "string", "status") {
		t.Error("missing or incorrect Status field (expected string for enum)")
	}

	// Should not import time, encoding/json, or uuid (none needed).
	if strings.Contains(result, `"time"`) {
		t.Error("unexpected time import")
	}
	if strings.Contains(result, `"encoding/json"`) {
		t.Error("unexpected encoding/json import")
	}
	if strings.Contains(result, `"github.com/google/uuid"`) {
		t.Error("unexpected uuid import")
	}
}

func TestGoTypesGenerator_EmptySchema(t *testing.T) {
	schema := &model.Schema{Tables: []model.Table{}}

	gen := &GoTypesGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	if !strings.Contains(result, "// Code generated by pgdesign -- do not edit.") {
		t.Error("missing generated header comment")
	}
	if !strings.Contains(result, "package schema") {
		t.Error("missing package declaration")
	}
	if strings.Contains(result, "import") {
		t.Error("empty schema should not have imports")
	}
	if strings.Contains(result, "type ") {
		t.Error("empty schema should not have type declarations")
	}
}

