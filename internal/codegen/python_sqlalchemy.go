package codegen

import (
	"bytes"
	"fmt"
	"sort"
	"strings"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// PythonSQLAlchemyGenerator generates SQLAlchemy 2.0 declarative models
// corresponding to database tables, including mapped_column definitions
// and relationship fields derived from foreign key metadata.
type PythonSQLAlchemyGenerator struct{}

// saTypeInfo holds the SQLAlchemy type mapping for a PostgreSQL column type.
type saTypeInfo struct {
	SAColumnType string // e.g., "String", "Integer", "UUID"
	PythonType   string // e.g., "str", "int", "uuid.UUID"
	SAImport     string // import name for sqlalchemy (e.g., "String", "BigInteger")
	PyImport     string // import token (e.g., "datetime", "uuid", "Decimal", "Any")
	IsDialect    bool   // true if the SA type comes from sqlalchemy.dialects.postgresql
}

// pgTypeToSA maps a column's PostgreSQL type to SQLAlchemy type info.
// enumsByName maps enum type names (bare and schema-qualified) to their
// model.Enum definitions; it covers both enum and state_machine kinds.
func pgTypeToSA(col model.Column, enumsByName map[string]*model.Enum) saTypeInfo {
	// Semantic type overrides. The money mapping lives here (not in codegen's
	// TypeResolver) because it selects the ORM column type (sa.Integer), not
	// the language type -- TypeResolver only resolves native/language types.
	if col.SemanticTypeName == "money" {
		return saTypeInfo{SAColumnType: "Integer", PythonType: "int", SAImport: "Integer"}
	}

	// Enum and state machine columns map to sa.Enum with the declared values.
	// The Python type stays str: sa.Enum stores/returns plain strings.
	if col.TypeKind == "enum" || col.TypeKind == "state_machine" {
		if e, ok := enumsByName[col.PGType.Base]; ok {
			parts := make([]string, 0, len(e.Values)+1)
			for _, v := range e.Values {
				parts = append(parts, fmt.Sprintf("%q", v))
			}
			parts = append(parts, fmt.Sprintf("name=%q", e.Name))
			return saTypeInfo{
				SAColumnType: "Enum(" + strings.Join(parts, ", ") + ")",
				PythonType:   "str",
				SAImport:     "Enum",
			}
		}
	}

	pgType := col.PGType.Base

	switch pgType {
	case "text", "varchar", "char", "bpchar":
		return saTypeInfo{SAColumnType: "String", PythonType: "str", SAImport: "String"}
	case "int4":
		return saTypeInfo{SAColumnType: "Integer", PythonType: "int", SAImport: "Integer"}
	case "int8":
		return saTypeInfo{SAColumnType: "BigInteger", PythonType: "int", SAImport: "BigInteger"}
	case "int2":
		return saTypeInfo{SAColumnType: "SmallInteger", PythonType: "int", SAImport: "SmallInteger"}
	case "bool":
		return saTypeInfo{SAColumnType: "Boolean", PythonType: "bool", SAImport: "Boolean"}
	case "timestamptz", "timestamp":
		return saTypeInfo{SAColumnType: "DateTime(timezone=True)", PythonType: "datetime", SAImport: "DateTime", PyImport: "datetime"}
	case "date":
		return saTypeInfo{SAColumnType: "DateTime", PythonType: "datetime", SAImport: "DateTime", PyImport: "datetime"}
	case "uuid":
		return saTypeInfo{SAColumnType: "UUID", PythonType: "uuid.UUID", IsDialect: true, PyImport: "uuid"}
	case "jsonb", "json":
		return saTypeInfo{SAColumnType: "JSONB", PythonType: "dict[str, Any]", IsDialect: true, PyImport: "Any"}
	case "numeric":
		return saTypeInfo{SAColumnType: "Numeric", PythonType: "Decimal", SAImport: "Numeric", PyImport: "Decimal"}
	case "float4", "float8":
		return saTypeInfo{SAColumnType: "Float", PythonType: "float", SAImport: "Float"}
	case "bytea":
		return saTypeInfo{SAColumnType: "LargeBinary", PythonType: "bytes", SAImport: "LargeBinary"}
	case "interval":
		return saTypeInfo{SAColumnType: "String", PythonType: "str", SAImport: "String"}
	default:
		// Enum types and anything unrecognized fall back to String/str.
		return saTypeInfo{SAColumnType: "String", PythonType: "str", SAImport: "String"}
	}
}

// Generate produces a Python source file with SQLAlchemy 2.0 declarative
// models: one class per table with mapped_column definitions and relationship
// fields for single-column foreign keys.
func (g *PythonSQLAlchemyGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	header := "# Code generated by pgdesign -- do not edit.\n"

	if len(schema.Tables) == 0 {
		buf.WriteString(header)
		return buf.Bytes(), nil
	}

	if schema.FKGraph == nil {
		schema.BuildFKGraph()
	}

	// Name -> enum lookup built from the schema.Enums slice; covers enum and
	// state_machine kinds (build.go appends state machines to Enums). Keyed by
	// both bare and schema-qualified names because introspection may
	// schema-qualify types outside the search path.
	enumsByName := make(map[string]*model.Enum, len(schema.Enums)*2)
	for i := range schema.Enums {
		e := &schema.Enums[i]
		enumsByName[e.Name] = e
		if e.Schema != "" {
			enumsByName[e.Schema+"."+e.Name] = e
		}
	}

	// First pass: collect column and relationship info per table, track imports.
	type colField struct {
		Name       string
		MappedType string // e.g., "Mapped[str]", "Mapped[Optional[int]]"
		MappedCol  string // e.g., `mapped_column(String, nullable=False)`
	}
	type relField struct {
		Name       string
		MappedType string // e.g., `Mapped["User"]`, `Mapped[list["Order"]]`
		RelCall    string // e.g., `relationship(back_populates="orders")`
	}
	type classInfo struct {
		Name      string
		TableName string
		Columns   []colField
		Rels      []relField
	}

	var classes []classInfo

	// Import tracking.
	saImports := make(map[string]bool)      // sqlalchemy core imports (String, Integer, etc.)
	dialectImports := make(map[string]bool) // sqlalchemy.dialects.postgresql (UUID, JSONB)
	pyStdImports := make(map[string]bool)   // Python stdlib import tokens
	needOptional := false
	needAny := false
	needText := false
	needForeignKey := false

	for _, tbl := range schema.Tables {
		ci := classInfo{
			Name:      toPascalCase(tbl.Name),
			TableName: tbl.Name,
		}

		pkSet := make(map[string]bool, len(tbl.PK))
		for _, pk := range tbl.PK {
			pkSet[pk] = true
		}

		// Build FK column lookup: column name -> "reftable.refcol".
		fkColRef := make(map[string]string)
		for _, fk := range tbl.FKs {
			if len(fk.Columns) == 1 {
				fkColRef[fk.Columns[0]] = fk.RefTable + "." + fk.RefColumns[0]
			}
		}

		// Generate column fields.
		for _, col := range tbl.Columns {
			info := pgTypeToSA(col, enumsByName)

			// Track imports.
			if info.SAImport != "" {
				saImports[info.SAImport] = true
			}
			if info.IsDialect {
				dialectImports[info.SAColumnType] = true
			}
			switch info.PyImport {
			case "datetime":
				pyStdImports["datetime"] = true
			case "uuid":
				pyStdImports["uuid"] = true
			case "Decimal":
				pyStdImports["Decimal"] = true
			case "Any":
				needAny = true
			}

			// Build Mapped type hint.
			pyType := info.PythonType
			if col.Array {
				pyType = "list[" + pyType + "]"
			}
			if !col.NotNull {
				needOptional = true
				pyType = "Optional[" + pyType + "]"
			}
			mappedType := "Mapped[" + pyType + "]"

			// Build mapped_column arguments.
			var args []string
			args = append(args, info.SAColumnType)

			if ref, ok := fkColRef[col.Name]; ok {
				needForeignKey = true
				args = append(args, fmt.Sprintf("ForeignKey(%q)", ref))
			}

			if pkSet[col.Name] {
				args = append(args, "primary_key=True")
			}

			if col.NotNull {
				args = append(args, "nullable=False")
			} else {
				args = append(args, "nullable=True")
			}

			if col.Default != nil {
				needText = true
				args = append(args, fmt.Sprintf("server_default=text(%q)", *col.Default))
			} else if col.DefaultExpr != "" {
				needText = true
				args = append(args, fmt.Sprintf("server_default=text(%q)", col.DefaultExpr))
			}

			mappedCol := "mapped_column(" + strings.Join(args, ", ") + ")"

			ci.Columns = append(ci.Columns, colField{
				Name:       col.Name,
				MappedType: mappedType,
				MappedCol:  mappedCol,
			})
		}

		// BelongsTo relationships (many-to-one): this table's single-column FKs.
		for _, fk := range tbl.FKs {
			if len(fk.Columns) != 1 {
				continue
			}
			refClassName := toPascalCase(fk.RefTable)
			// Field name: strip _id suffix from FK column if present, else use ref table name.
			fieldName := fk.Columns[0]
			if strings.HasSuffix(fieldName, "_id") {
				fieldName = strings.TrimSuffix(fieldName, "_id")
			} else {
				fieldName = fk.RefTable
			}
			// back_populates points to the one-to-many field on the referenced table,
			// which uses this table's name.
			backPopulates := tbl.Name

			ci.Rels = append(ci.Rels, relField{
				Name:       fieldName,
				MappedType: fmt.Sprintf("Mapped[\"%s\"]", refClassName),
				RelCall:    fmt.Sprintf("relationship(back_populates=%q)", backPopulates),
			})
		}

		// HasMany relationships (one-to-many): other tables' single-column FKs
		// pointing to this table.
		if edges := schema.FKGraph.Reverse[tbl.Name]; len(edges) > 0 {
			fkColCount := make(map[string]int)
			for _, e := range edges {
				fkColCount[e.FKName]++
			}
			seen := make(map[string]bool)
			for _, e := range edges {
				if fkColCount[e.FKName] != 1 || seen[e.FKName] {
					continue
				}
				seen[e.FKName] = true
				fkClassName := toPascalCase(e.FromTable)
				fieldName := e.FromTable
				backPopulates := e.FromColumn
				if strings.HasSuffix(backPopulates, "_id") {
					backPopulates = strings.TrimSuffix(backPopulates, "_id")
				} else {
					backPopulates = tbl.Name
				}

				ci.Rels = append(ci.Rels, relField{
					Name:       fieldName,
					MappedType: fmt.Sprintf("Mapped[list[\"%s\"]]", fkClassName),
					RelCall:    fmt.Sprintf("relationship(back_populates=%q)", backPopulates),
				})
			}
		}

		classes = append(classes, ci)
	}

	// Write output.
	buf.WriteString(header)

	// Future annotations (always).
	buf.WriteString("\nfrom __future__ import annotations\n")

	// Stdlib imports.
	var stdlibLines []string
	if pyStdImports["uuid"] {
		stdlibLines = append(stdlibLines, "import uuid")
	}
	if pyStdImports["datetime"] {
		stdlibLines = append(stdlibLines, "from datetime import datetime")
	}
	if pyStdImports["Decimal"] {
		stdlibLines = append(stdlibLines, "from decimal import Decimal")
	}
	// Typing imports.
	var typingItems []string
	if needAny {
		typingItems = append(typingItems, "Any")
	}
	if needOptional {
		typingItems = append(typingItems, "Optional")
	}
	if len(typingItems) > 0 {
		stdlibLines = append(stdlibLines, "from typing import "+strings.Join(typingItems, ", "))
	}
	if len(stdlibLines) > 0 {
		buf.WriteString("\n")
		for _, line := range stdlibLines {
			buf.WriteString(line)
			buf.WriteString("\n")
		}
	}

	// SQLAlchemy imports.
	buf.WriteString("\n")

	// Core SA types.
	var saTypeList []string
	for imp := range saImports {
		saTypeList = append(saTypeList, imp)
	}
	if needForeignKey {
		saTypeList = append(saTypeList, "ForeignKey")
	}
	if needText {
		saTypeList = append(saTypeList, "text")
	}
	sort.Strings(saTypeList)
	if len(saTypeList) > 0 {
		fmt.Fprintf(&buf, "from sqlalchemy import %s\n", strings.Join(saTypeList, ", "))
	}

	// Dialect imports.
	var dialectList []string
	for imp := range dialectImports {
		dialectList = append(dialectList, imp)
	}
	sort.Strings(dialectList)
	if len(dialectList) > 0 {
		fmt.Fprintf(&buf, "from sqlalchemy.dialects.postgresql import %s\n", strings.Join(dialectList, ", "))
	}

	// ORM imports (always needed).
	buf.WriteString("from sqlalchemy.orm import DeclarativeBase, Mapped, mapped_column, relationship\n")

	// Base class.
	buf.WriteString("\n\nclass Base(DeclarativeBase):\n    pass\n")

	// Table classes.
	for _, ci := range classes {
		buf.WriteString("\n\n")
		fmt.Fprintf(&buf, "class %s(Base):\n", ci.Name)
		fmt.Fprintf(&buf, "    __tablename__ = %q\n", ci.TableName)

		// Column fields.
		for _, col := range ci.Columns {
			buf.WriteString("\n")
			fmt.Fprintf(&buf, "    %s: %s = %s", col.Name, col.MappedType, col.MappedCol)
		}

		// Relationship fields, separated by a blank line from columns.
		if len(ci.Rels) > 0 {
			buf.WriteString("\n")
			for _, rel := range ci.Rels {
				buf.WriteString("\n")
				fmt.Fprintf(&buf, "    %s: %s = %s", rel.Name, rel.MappedType, rel.RelCall)
			}
		}

		buf.WriteString("\n")
	}

	return buf.Bytes(), nil
}
