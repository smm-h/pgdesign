func TestPythonConstraintsGenerator_Generate(t *testing.T) {
	schema := &model.Schema{
		Name: "app",
		Enums: []model.Enum{
			{Name: "role", Values: []string{"admin", "user", "guest"}},
		},
		Tables: []model.Table{
			{
				Name: "users",
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.MustParse("bigint"), NotNull: true},
					{Name: "name", PGType: typeinfo.MustParse("text"), NotNull: true},
					{Name: "email", PGType: typeinfo.MustParse("text"), NotNull: true},
					{Name: "role", PGType: typeinfo.MustParse("role"), NotNull: true, TypeKind: "enum"},
					{Name: "age", PGType: typeinfo.MustParse("integer"), NotNull: true},
					{Name: "slug", PGType: typeinfo.MustParse("varchar"), NotNull: true},
				},
				PK: []string{"id"},
				Checks: []model.CheckConstraint{
					{Name: "ck_users_age_range", Expr: "age >= 0 AND age <= 150"},
					{Name: "ck_users_email_fmt", Expr: "email LIKE '%@%'"},
					{Name: "ck_users_slug_len", Expr: "LENGTH(slug) <= 100"},
				},
			},
		},
	}

	gen := &PythonConstraintsGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Header.
	if !strings.Contains(result, "Code generated by pgdesign") {
		t.Error("missing header comment")
	}

	// ValidationError class.
	if !strings.Contains(result, "class ValidationError") {
		t.Error("missing ValidationError class")
	}

	// Validate function signature.
	if !strings.Contains(result, "def validate_users(row: Users) -> list[ValidationError]:") {
		t.Error("missing validate_users function signature")
	}

	// NOT NULL checks.
	if !strings.Contains(result, "row.name is None") {
		t.Error("missing NOT NULL check for name")
	}
	if !strings.Contains(result, "row.email is None") {
		t.Error("missing NOT NULL check for email")
	}
	if !strings.Contains(result, "row.slug is None") {
		t.Error("missing NOT NULL check for slug")
	}

	// Enum check.
	if !strings.Contains(result, `"admin"`) {
		t.Error("missing enum value 'admin'")
	}
	if !strings.Contains(result, `"user"`) {
		t.Error("missing enum value 'user'")
	}
	if !strings.Contains(result, `"guest"`) {
		t.Error("missing enum value 'guest'")
	}
	if !strings.Contains(result, "not in {") {
		t.Error("missing set membership check for enum")
	}

	// Range check for age.
	if !strings.Contains(result, "row.age") {
		t.Error("missing age range check")
	}
	if !strings.Contains(result, "150") {
		t.Error("missing age upper bound 150")
	}

	// LIKE check for email -> re.fullmatch.
	if !strings.Contains(result, "re.fullmatch") {
		t.Error("missing re.fullmatch for email LIKE check")
	}
	if !strings.Contains(result, "import re") {
		t.Error("missing re import")
	}

	// Length check for slug.
	if !strings.Contains(result, "len(row.slug)") {
		t.Error("missing length check for slug")
	}
	if !strings.Contains(result, "100") {
		t.Error("missing length limit 100")
	}
}

func TestPythonConstraintsGenerator_EmptySchema(t *testing.T) {
	schema := &model.Schema{
		Name: "empty",
	}

	gen := &PythonConstraintsGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)
	if !strings.Contains(result, "No tables with extractable constraints.") {
		t.Error("missing empty-schema comment")
	}
	if strings.Contains(result, "def validate_") {
		t.Error("should not contain any validate functions for empty schema")
	}
}

