package codegen

import (
	"bytes"
	"fmt"
	"strings"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// PythonConstraintsGenerator generates Python validation functions for table constraints.
type PythonConstraintsGenerator struct{}

// Generate produces a Python file with validate_<table_name> functions for each
// table that has extractable constraints (NOT NULL, enum, CHECK, JSON schema).
func (g *PythonConstraintsGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	buf.WriteString("# Code generated by pgdesign -- do not edit.\n")
	buf.WriteString("# Regenerate with: pgdesign codegen --lang python --mode constraints <schema-files>\n")
	if schema.Name != "" {
		fmt.Fprintf(&buf, "# Schema: %s\n", schema.Name)
	}

	needsRegexp := false

	// Collect tables with constraints.
	type tableWork struct {
		table model.Table
		cs    ConstraintSet
	}
	var work []tableWork
	for _, tbl := range schema.Tables {
		cs := ExtractConstraints(tbl, *schema)
		if !cs.HasConstraints() {
			continue
		}
		work = append(work, tableWork{table: tbl, cs: cs})
		for _, expr := range cs.CheckExprs {
			if pat := classifyCheck(expr); pat != nil {
				if _, ok := pat.(*likePattern); ok {
					needsRegexp = true
				}
			}
		}
	}

	if len(work) == 0 {
		buf.WriteString("\n# No tables with extractable constraints.\n")
		return buf.Bytes(), nil
	}

	// Write imports.
	buf.WriteString("\nfrom __future__ import annotations\n\n")
	buf.WriteString("from dataclasses import dataclass\n")
	if needsRegexp {
		buf.WriteString("import re\n")
	}

	// Write ValidationError type.
	buf.WriteString(`

@dataclass(frozen=True, slots=True)
class ValidationError:
    """Describes a single constraint violation."""
    field: str
    message: str
`)

	// Generate per-table validators.
	for _, tw := range work {
		writePythonValidator(&buf, tw.table, tw.cs)
	}

	return buf.Bytes(), nil
}

func writePythonValidator(buf *bytes.Buffer, table model.Table, cs ConstraintSet) {
	funcName := "validate_" + table.Name
	typeName := toPascalCase(table.Name)

	fmt.Fprintf(buf, "\n\ndef %s(row: %s) -> list[ValidationError]:\n", funcName, typeName)
	fmt.Fprintf(buf, "    \"\"\"Validate constraint rules for the %s table.\"\"\"\n", table.Name)
	buf.WriteString("    errors: list[ValidationError] = []\n")

	// NOT NULL checks.
	for _, col := range cs.NotNullFields {
		fmt.Fprintf(buf, "    if row.%s is None:\n", col)
		fmt.Fprintf(buf, "        errors.append(ValidationError(field=%q, message=\"must not be empty\"))\n", col)
	}

	// Enum checks.
	for col, values := range cs.EnumFields {
		quoted := make([]string, len(values))
		for i, v := range values {
			quoted[i] = fmt.Sprintf("%q", v)
		}
		fmt.Fprintf(buf, "    if row.%s is not None and row.%s not in {%s}:\n",
			col, col, strings.Join(quoted, ", "))
		fmt.Fprintf(buf, "        errors.append(ValidationError(field=%q, message=f\"invalid value {row.%s!r}\"))\n", col, col)
	}

	// CHECK constraint checks.
	for col, expr := range cs.CheckExprs {
		pat := classifyCheck(expr)
		if pat == nil {
			fmt.Fprintf(buf, "    # CHECK on %s: %s (unrecognized pattern, skipped)\n", col, expr)
			continue
		}
		writePythonCheckPattern(buf, col, pat)
	}

	buf.WriteString("    return errors\n")
}

func writePythonCheckPattern(buf *bytes.Buffer, col string, pat checkPattern) {
	switch p := pat.(type) {
	case *rangePattern:
		var lowOp, highOp string
		if p.LowIncl {
			lowOp = "<"
		} else {
			lowOp = "<="
		}
		if p.HighIncl {
			highOp = ">"
		} else {
			highOp = ">="
		}
		fmt.Fprintf(buf, "    if row.%s is not None and (row.%s %s %s or row.%s %s %s):\n",
			col, col, lowOp, p.Low, col, highOp, p.High)
		fmt.Fprintf(buf, "        errors.append(ValidationError(field=%q, message=\"value out of range\"))\n", col)

	case *comparisonPattern:
		failOp := invertComparisonOp(p.Op)
		pyOp := failOp
		if pyOp == "!=" {
			pyOp = "!="
		} else if pyOp == "==" {
			pyOp = "=="
		}
		fmt.Fprintf(buf, "    if row.%s is not None and row.%s %s %s:\n", col, col, pyOp, p.Value)
		fmt.Fprintf(buf, "        errors.append(ValidationError(field=%q, message=\"failed check: %s %s\"))\n", col, p.Op, p.Value)

	case *lengthPattern:
		failOp := invertComparisonOp(p.Op)
		fmt.Fprintf(buf, "    if row.%s is not None and len(row.%s) %s %d:\n", col, col, failOp, p.Value)
		fmt.Fprintf(buf, "        errors.append(ValidationError(field=%q, message=f\"length must be %s %d, got {len(row.%s)}\"))\n", col, p.Op, p.Value, col)

	case *likePattern:
		regex := likeToRegex(p.Pattern)
		flagsArg := ""
		if p.IsCaseInsensitive() {
			flagsArg = ", re.IGNORECASE"
		}
		if p.IsNegated() {
			fmt.Fprintf(buf, "    if row.%s is not None and re.fullmatch(r\"%s\", row.%s%s):\n", col, regex, col, flagsArg)
		} else {
			fmt.Fprintf(buf, "    if row.%s is not None and not re.fullmatch(r\"%s\", row.%s%s):\n", col, regex, col, flagsArg)
		}
		fmt.Fprintf(buf, "        errors.append(ValidationError(field=%q, message=\"does not match required pattern\"))\n", col)
	}
}
