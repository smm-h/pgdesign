package codegen

import (
	"bytes"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// EnumsGenerator generates standalone enum definitions for all enums in a schema.
// Unlike the types generator which embeds enums alongside struct/class definitions,
// this produces a file containing only enum types.
type EnumsGenerator struct {
	Lang Lang
}

// Generate produces a source file with enum definitions for all enums in the schema.
func (g *EnumsGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer

	// Write header.
	commentPrefix := "// "
	if g.Lang == LangPython {
		commentPrefix = "# "
	}
	buf.WriteString(commentPrefix + "Generated by pgdesign -- do not edit manually.\n")
	buf.WriteString(commentPrefix + "Regenerate with: pgdesign codegen --lang " + string(g.Lang) + " --mode enums <schema-files>\n")

	enumBlock := GenerateEnums(schema.Enums, g.Lang)
	if enumBlock == "" {
		return buf.Bytes(), nil
	}

	// Write language-specific preamble.
	if g.Lang == LangPython {
		buf.WriteString("\nfrom enum import StrEnum\n")
	}

	buf.WriteString("\n")
	buf.WriteString(enumBlock)

	return buf.Bytes(), nil
}
