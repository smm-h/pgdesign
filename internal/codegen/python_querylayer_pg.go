// generatePerTablePgFile produces a _<table>_pg.py file for a single table.
// The file contains a class <PascalCase>Pg that implements the table's Writer
// and Reader protocol methods using asyncpg parameterized queries.
func generatePerTablePgFile(tbl *model.Table, schema *model.Schema, smMap map[string]*model.SMTransitionMap) []byte {
	var buf bytes.Buffer
	className := toPascalCase(tbl.Name)
	rowType := className + "Row"

	// Collect imports.
	imports := newImportCollector()
	for _, col := range tbl.Columns {
		m := LookupType(col.PGType, LangPython)
		imports.addFromMapping(m, col)
	}
	for _, col := range tbl.Columns {
		if smt, ok := smMap[col.SemanticTypeName]; ok {
			for _, nt := range smt.NamedTransitions {
				for _, pgType := range sortedMapKeys(nt.Requires) {
					m := LookupType(nt.Requires[pgType], LangPython)
					imports.addFromType(m)
				}
			}
		}
	}

	// Header.
	buf.WriteString("# Generated by pgdesign -- do not edit manually.\n")
	buf.WriteString("# Regenerate with: pgdesign codegen --lang python --mode query-layer <schema-files>\n")

	// Imports.
	buf.WriteString("\nfrom __future__ import annotations\n")
	buf.WriteString("\nfrom typing import Optional\n")

	if imports.needs["datetime"] {
		buf.WriteString("from datetime import datetime\n")
	}
	if imports.needs["Decimal"] {
		buf.WriteString("from decimal import Decimal\n")
	}
	if imports.needs["UUID"] {
		buf.WriteString("from uuid import UUID\n")
	}

	fmt.Fprintf(&buf, "\nfrom .protocols import PgContext, %s\n", rowType)

	// Class.
	fmt.Fprintf(&buf, "\n\nclass %sPg:\n", className)
	fmt.Fprintf(&buf, "    \"\"\"asyncpg delegate for the %s table.\"\"\"\n", tbl.Name)
	buf.WriteString("\n    def __init__(self, ctx: PgContext) -> None:\n")
	buf.WriteString("        self._ctx = ctx\n")

	// create method.
	writePgCreateMethod(&buf, tbl)

	// get method.
	writePgGetMethod(&buf, tbl, rowType)

	// update method (skip if append-only).
	if !tbl.AppendOnly {
		writePgUpdateMethod(&buf, tbl)
	}

	// delete method (skip if append-only).
	if !tbl.AppendOnly {
		writePgDeleteMethod(&buf, tbl)
	}

	// get_by_fk methods.
	writePgGetByFKMethods(&buf, tbl, schema.FKGraph, rowType)

	// get_by_unique methods.
	writePgGetByUniqueMethods(&buf, tbl, rowType)

	// list method.
	writePgListMethod(&buf, tbl, rowType)

	// SM transition methods.
	writePgSMTransitionMethods(&buf, tbl, smMap)

	return buf.Bytes()
}

// writePgCreateMethod generates INSERT INTO ... RETURNING with parameterized queries.
func writePgCreateMethod(buf *bytes.Buffer, tbl *model.Table) {
	pkCols := pkColumns(tbl)
	if len(pkCols) == 0 {
		return
	}

	var required []model.Column
	var optional []model.Column
	for _, col := range tbl.Columns {
		if isAutoColumn(col, tbl.PK) {
			continue
		}
		if col.Generated != "" {
			continue
		}
		if col.Identity != "" {
			continue
		}
		hasDefault := col.Default != nil || col.DefaultExpr != ""
		if col.NotNull && !hasDefault {
			required = append(required, col)
		} else {
			optional = append(optional, col)
		}
	}

	allWritable := append(required, optional...)
	returnType := pkReturnType(tbl)

	// Method signature.
	fmt.Fprintf(buf, "\n    async def create_%s(\n        self,\n        *,\n", tbl.Name)
	for _, col := range required {
		pyType := columnPythonType(col)
		fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
	}
	for _, col := range optional {
		pyType := baseColumnPythonType(col)
		optType := ApplyNullable(pyType, LangPython)
		fmt.Fprintf(buf, "        %s: %s = None,\n", col.Name, optType)
	}
	fmt.Fprintf(buf, "    ) -> %s:\n", returnType)

	// SQL: INSERT INTO <table> (<cols>) VALUES ($1, $2, ...) RETURNING <pk>
	var colNames []string
	var params []string
	for i, col := range allWritable {
		colNames = append(colNames, col.Name)
		params = append(params, fmt.Sprintf("$%d", i+1))
	}

	var pkNames []string
	for _, pk := range tbl.PK {
		pkNames = append(pkNames, pk)
	}

	sql := fmt.Sprintf("INSERT INTO %s (%s) VALUES (%s) RETURNING %s",
		tbl.Name,
		strings.Join(colNames, ", "),
		strings.Join(params, ", "),
		strings.Join(pkNames, ", "))

	fmt.Fprintf(buf, "        sql = \"%s\"\n", sql)
	fmt.Fprintf(buf, "        return await self._ctx.pool.fetchval(sql, %s)\n",
		strings.Join(colNames, ", "))
}

// writePgGetMethod generates SELECT * FROM <table> WHERE <pk> = $1.
func writePgGetMethod(buf *bytes.Buffer, tbl *model.Table, rowType string) {
	pkCols := pkColumns(tbl)
	if len(pkCols) == 0 {
		return
	}

	fmt.Fprintf(buf, "\n    async def get_%s(\n        self,\n", tbl.Name)
	for _, col := range pkCols {
		pyType := columnPythonType(col)
		fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
	}
	fmt.Fprintf(buf, "    ) -> Optional[%s]:\n", rowType)

	// Build WHERE clause.
	var whereParts []string
	for i, col := range pkCols {
		whereParts = append(whereParts, fmt.Sprintf("%s = $%d", col.Name, i+1))
	}

	sql := fmt.Sprintf("SELECT * FROM %s WHERE %s",
		tbl.Name, strings.Join(whereParts, " AND "))

	fmt.Fprintf(buf, "        sql = \"%s\"\n", sql)
	fmt.Fprintf(buf, "        row = await self._ctx.pool.fetchrow(sql, %s)\n",
		joinColumnNames(pkCols))
	fmt.Fprintf(buf, "        return %s(**dict(row)) if row else None\n", rowType)
}

// writePgUpdateMethod generates UPDATE ... SET from non-None kwargs.
func writePgUpdateMethod(buf *bytes.Buffer, tbl *model.Table) {
	pkCols := pkColumns(tbl)
	if len(pkCols) == 0 {
		return
	}

	// Collect updatable columns.
	var updatableCols []model.Column
	for _, col := range tbl.Columns {
		if isPKColumn(col.Name, tbl.PK) {
			continue
		}
		if col.Generated != "" {
			continue
		}
		if col.Identity != "" {
			continue
		}
		updatableCols = append(updatableCols, col)
	}

	fmt.Fprintf(buf, "\n    async def update_%s(\n        self,\n", tbl.Name)
	for _, col := range pkCols {
		pyType := columnPythonType(col)
		fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
	}
	buf.WriteString("        *,\n")
	for _, col := range updatableCols {
		pyType := baseColumnPythonType(col)
		optType := ApplyNullable(pyType, LangPython)
		fmt.Fprintf(buf, "        %s: %s = None,\n", col.Name, optType)
	}
	buf.WriteString("    ) -> None:\n")

	// Dynamic SET clause building.
	buf.WriteString("        clauses: list[str] = []\n")
	buf.WriteString("        args: list = []\n")
	buf.WriteString("        idx = 1\n")
	for _, col := range updatableCols {
		fmt.Fprintf(buf, "        if %s is not None:\n", col.Name)
		fmt.Fprintf(buf, "            clauses.append(f\"%s = ${idx}\")\n", col.Name)
		fmt.Fprintf(buf, "            args.append(%s)\n", col.Name)
		buf.WriteString("            idx += 1\n")
	}
	buf.WriteString("        if not clauses:\n")
	buf.WriteString("            return\n")

	// PK params at end.
	for _, col := range pkCols {
		fmt.Fprintf(buf, "        args.append(%s)\n", col.Name)
	}

	var pkWhereParts []string
	for i := range pkCols {
		pkWhereParts = append(pkWhereParts, fmt.Sprintf("%s = ${idx + %d}", pkCols[i].Name, i))
	}
	fmt.Fprintf(buf, "        sql = f\"UPDATE %s SET {', '.join(clauses)} WHERE %s\"\n",
		tbl.Name, strings.Join(pkWhereParts, " AND "))
	buf.WriteString("        await self._ctx.pool.execute(sql, *args)\n")
}

// writePgDeleteMethod generates DELETE FROM <table> WHERE <pk> = $1 RETURNING true.
func writePgDeleteMethod(buf *bytes.Buffer, tbl *model.Table) {
	pkCols := pkColumns(tbl)
	if len(pkCols) == 0 {
		return
	}

	fmt.Fprintf(buf, "\n    async def delete_%s(\n        self,\n", tbl.Name)
	for _, col := range pkCols {
		pyType := columnPythonType(col)
		fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
	}
	buf.WriteString("    ) -> bool:\n")

	var whereParts []string
	for i, col := range pkCols {
		whereParts = append(whereParts, fmt.Sprintf("%s = $%d", col.Name, i+1))
	}

	sql := fmt.Sprintf("DELETE FROM %s WHERE %s RETURNING true",
		tbl.Name, strings.Join(whereParts, " AND "))

	fmt.Fprintf(buf, "        sql = \"%s\"\n", sql)
	fmt.Fprintf(buf, "        result = await self._ctx.pool.fetchval(sql, %s)\n",
		joinColumnNames(pkCols))
	buf.WriteString("        return result is True\n")
}

// writePgGetByFKMethods generates SELECT * FROM <table> WHERE <fk_target_col> = $1.
func writePgGetByFKMethods(buf *bytes.Buffer, tbl *model.Table, fkGraph *model.FKGraph, rowType string) {
	if fkGraph == nil {
		return
	}

	type fkByMethod struct {
		paramName string // edge.FromColumn (used as param name and method suffix)
		queryCol  string // edge.ToColumn (actual column on this table)
		pyType    string
	}
	seen := make(map[string]bool)
	var methods []fkByMethod

	for _, edge := range fkGraph.Reverse[tbl.Name] {
		methodName := edge.FromColumn
		if seen[methodName] {
			continue
		}
		seen[methodName] = true

		pyType := "Any"
		if toCol := qlFindColumn(tbl, edge.ToColumn); toCol != nil {
			pyType = columnPythonType(*toCol)
		}

		methods = append(methods, fkByMethod{
			paramName: methodName,
			queryCol:  edge.ToColumn,
			pyType:    pyType,
		})
	}

	sort.Slice(methods, func(i, j int) bool {
		return methods[i].paramName < methods[j].paramName
	})

	for _, fm := range methods {
		fmt.Fprintf(buf, "\n    async def get_%s_by_%s(\n        self,\n        %s: %s,\n    ) -> list[%s]:\n",
			tbl.Name, fm.paramName, fm.paramName, fm.pyType, rowType)
		sql := fmt.Sprintf("SELECT * FROM %s WHERE %s = $1", tbl.Name, fm.queryCol)
		fmt.Fprintf(buf, "        sql = \"%s\"\n", sql)
		fmt.Fprintf(buf, "        rows = await self._ctx.pool.fetch(sql, %s)\n", fm.paramName)
		fmt.Fprintf(buf, "        return [%s(**dict(r)) for r in rows]\n", rowType)
	}
}

// writePgGetByUniqueMethods generates SELECT * FROM <table> WHERE <cols> = $N.
func writePgGetByUniqueMethods(buf *bytes.Buffer, tbl *model.Table, rowType string) {
	for _, uq := range tbl.Uniques {
		suffix := strings.Join(uq.Columns, "_")
		fmt.Fprintf(buf, "\n    async def get_%s_by_%s(\n        self,\n", tbl.Name, suffix)
		for _, colName := range uq.Columns {
			col := qlFindColumn(tbl, colName)
			if col != nil {
				pyType := columnPythonType(*col)
				fmt.Fprintf(buf, "        %s: %s,\n", colName, pyType)
			} else {
				fmt.Fprintf(buf, "        %s: Any,\n", colName)
			}
		}
		fmt.Fprintf(buf, "    ) -> Optional[%s]:\n", rowType)

		var whereParts []string
		var paramNames []string
		for i, colName := range uq.Columns {
			whereParts = append(whereParts, fmt.Sprintf("%s = $%d", colName, i+1))
			paramNames = append(paramNames, colName)
		}

		sql := fmt.Sprintf("SELECT * FROM %s WHERE %s", tbl.Name, strings.Join(whereParts, " AND "))
		fmt.Fprintf(buf, "        sql = \"%s\"\n", sql)
		fmt.Fprintf(buf, "        row = await self._ctx.pool.fetchrow(sql, %s)\n",
			strings.Join(paramNames, ", "))
		fmt.Fprintf(buf, "        return %s(**dict(row)) if row else None\n", rowType)
	}
}

// writePgListMethod generates SELECT * FROM <table> ORDER BY <pk> LIMIT $1 OFFSET $2.
func writePgListMethod(buf *bytes.Buffer, tbl *model.Table, rowType string) {
	var pkNames []string
	for _, pk := range tbl.PK {
		pkNames = append(pkNames, pk)
	}

	orderBy := strings.Join(pkNames, ", ")
	if orderBy == "" {
		orderBy = "1" // fallback for tables without PK
	}

	fmt.Fprintf(buf, "\n    async def list_%s(\n        self,\n        limit: int = 100,\n        offset: int = 0,\n    ) -> list[%s]:\n",
		tbl.Name, rowType)

	sql := fmt.Sprintf("SELECT * FROM %s ORDER BY %s LIMIT $1 OFFSET $2", tbl.Name, orderBy)
	fmt.Fprintf(buf, "        sql = \"%s\"\n", sql)
	buf.WriteString("        rows = await self._ctx.pool.fetch(sql, limit, offset)\n")
	fmt.Fprintf(buf, "        return [%s(**dict(r)) for r in rows]\n", rowType)
}

// writePgSMTransitionMethods generates state machine transition methods that
// SELECT FOR UPDATE, validate the transition, then UPDATE.
func writePgSMTransitionMethods(buf *bytes.Buffer, tbl *model.Table, smMap map[string]*model.SMTransitionMap) {
	pkCols := pkColumns(tbl)
	if len(pkCols) == 0 {
		return
	}

	for _, col := range tbl.Columns {
		smt, ok := smMap[col.SemanticTypeName]
		if !ok {
			continue
		}
		for _, nt := range smt.NamedTransitions {
			fmt.Fprintf(buf, "\n    async def %s_%s(\n        self,\n", nt.Name, tbl.Name)
			for _, pkCol := range pkCols {
				pyType := columnPythonType(pkCol)
				fmt.Fprintf(buf, "        %s: %s,\n", pkCol.Name, pyType)
			}

			if len(nt.Requires) > 0 {
				buf.WriteString("        *,\n")
				for _, paramName := range sortedMapKeys(nt.Requires) {
					pgType := nt.Requires[paramName]
					m := LookupType(pgType, LangPython)
					fmt.Fprintf(buf, "        %s: %s,\n", paramName, m.Type)
				}
			}
			buf.WriteString("    ) -> None:\n")

			// SELECT FOR UPDATE.
			var pkWhere []string
			for i, pkCol := range pkCols {
				pkWhere = append(pkWhere, fmt.Sprintf("%s = $%d", pkCol.Name, i+1))
			}
			selectSQL := fmt.Sprintf("SELECT %s FROM %s WHERE %s FOR UPDATE",
				col.Name, tbl.Name, strings.Join(pkWhere, " AND "))
			fmt.Fprintf(buf, "        select_sql = \"%s\"\n", selectSQL)
			fmt.Fprintf(buf, "        current = await self._ctx.pool.fetchval(select_sql, %s)\n",
				joinColumnNames(pkCols))

			// Validate transition.
			var fromStates []string
			for _, from := range nt.From {
				fromStates = append(fromStates, fmt.Sprintf("%q", from))
			}
			fmt.Fprintf(buf, "        allowed_from = {%s}\n", strings.Join(fromStates, ", "))
			buf.WriteString("        if current not in allowed_from:\n")
			fmt.Fprintf(buf, "            raise ValueError(f\"Cannot %s: current state {current!r} not in {allowed_from}\")\n",
				nt.Name)

			// UPDATE.
			paramIdx := len(pkCols) + 1
			var setParts []string
			setParts = append(setParts, fmt.Sprintf("%s = $%d", col.Name, paramIdx))
			var updateArgs []string
			updateArgs = append(updateArgs, joinColumnNames(pkCols))
			updateArgs = append(updateArgs, fmt.Sprintf("%q", nt.To))

			// Extra requires params.
			if len(nt.Requires) > 0 {
				paramIdx++
				for _, paramName := range sortedMapKeys(nt.Requires) {
					setParts = append(setParts, fmt.Sprintf("%s = $%d", paramName, paramIdx))
					updateArgs = append(updateArgs, paramName)
					paramIdx++
				}
			}

			updateSQL := fmt.Sprintf("UPDATE %s SET %s WHERE %s",
				tbl.Name, strings.Join(setParts, ", "), strings.Join(pkWhere, " AND "))
			fmt.Fprintf(buf, "        update_sql = \"%s\"\n", updateSQL)
			fmt.Fprintf(buf, "        await self._ctx.pool.execute(update_sql, %s)\n",
				strings.Join(updateArgs, ", "))
		}
	}
}

// generatePgBackendFile produces pg_backend.py with the composite PgBackend class.
func generatePgBackendFile(tables []model.Table, schema *model.Schema, smMap map[string]*model.SMTransitionMap) []byte {
	var buf bytes.Buffer

	// Header.
	buf.WriteString("# Generated by pgdesign -- do not edit manually.\n")
	buf.WriteString("# Regenerate with: pgdesign codegen --lang python --mode query-layer <schema-files>\n")

	buf.WriteString("\nfrom __future__ import annotations\n")
	buf.WriteString("\nfrom typing import Any, Optional\n")

	// Collect imports needed for type annotations.
	imports := newImportCollector()
	for _, tbl := range tables {
		for _, col := range tbl.Columns {
			m := LookupType(col.PGType, LangPython)
			imports.addFromMapping(m, col)
		}
		for _, col := range tbl.Columns {
			if smt, ok := smMap[col.SemanticTypeName]; ok {
				for _, nt := range smt.NamedTransitions {
					for _, pgType := range sortedMapKeys(nt.Requires) {
						m := LookupType(nt.Requires[pgType], LangPython)
						imports.addFromType(m)
					}
				}
			}
		}
	}

	if imports.needs["datetime"] {
		buf.WriteString("from datetime import datetime\n")
	}
	if imports.needs["Decimal"] {
		buf.WriteString("from decimal import Decimal\n")
	}
	if imports.needs["UUID"] {
		buf.WriteString("from uuid import UUID\n")
	}

	// Import Row types from protocols.
	var rowImports []string
	for _, tbl := range tables {
		rowImports = append(rowImports, toPascalCase(tbl.Name)+"Row")
	}
	fmt.Fprintf(&buf, "\nfrom .protocols import PgContext, %s\n", strings.Join(rowImports, ", "))

	// Import per-table delegates.
	for _, tbl := range tables {
		className := toPascalCase(tbl.Name) + "Pg"
		fmt.Fprintf(&buf, "from ._%s_pg import %s\n", tbl.Name, className)
	}

	// PgBackend class.
	buf.WriteString("\n\nclass PgBackend:\n")
	buf.WriteString("    \"\"\"Composite backend delegating to per-table asyncpg implementations.\"\"\"\n")

	// __init__.
	buf.WriteString("\n    def __init__(self, pool: Any) -> None:\n")
	buf.WriteString("        ctx = PgContext(pool=pool)\n")
	for _, tbl := range tables {
		className := toPascalCase(tbl.Name) + "Pg"
		attrName := "_" + tbl.Name
		fmt.Fprintf(&buf, "        self.%s = %s(ctx)\n", attrName, className)
	}

	// Forwarding methods for all tables.
	for _, tbl := range tables {
		writePgBackendForwardingMethods(&buf, &tbl, schema, smMap)
	}

	return buf.Bytes()
}

// writePgBackendForwardingMethods generates one-line forwarding methods on PgBackend
// for all protocol methods of a table.
func writePgBackendForwardingMethods(buf *bytes.Buffer, tbl *model.Table, schema *model.Schema, smMap map[string]*model.SMTransitionMap) {
	className := toPascalCase(tbl.Name)
	rowType := className + "Row"
	attrName := "_" + tbl.Name

	pkCols := pkColumns(tbl)

	// create forwarding.
	if len(pkCols) > 0 {
		var required []model.Column
		var optional []model.Column
		for _, col := range tbl.Columns {
			if isAutoColumn(col, tbl.PK) {
				continue
			}
			if col.Generated != "" {
				continue
			}
			if col.Identity != "" {
				continue
			}
			hasDefault := col.Default != nil || col.DefaultExpr != ""
			if col.NotNull && !hasDefault {
				required = append(required, col)
			} else {
				optional = append(optional, col)
			}
		}

		returnType := pkReturnType(tbl)
		fmt.Fprintf(buf, "\n    async def create_%s(\n        self,\n        *,\n", tbl.Name)
		for _, col := range required {
			pyType := columnPythonType(col)
			fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
		}
		for _, col := range optional {
			pyType := baseColumnPythonType(col)
			optType := ApplyNullable(pyType, LangPython)
			fmt.Fprintf(buf, "        %s: %s = None,\n", col.Name, optType)
		}
		fmt.Fprintf(buf, "    ) -> %s:\n", returnType)

		// Forward call with keyword args.
		allWritable := append(required, optional...)
		var kwArgs []string
		for _, col := range allWritable {
			kwArgs = append(kwArgs, fmt.Sprintf("%s=%s", col.Name, col.Name))
		}
		fmt.Fprintf(buf, "        return await self.%s.create_%s(%s)\n",
			attrName, tbl.Name, strings.Join(kwArgs, ", "))
	}

	// get forwarding.
	if len(pkCols) > 0 {
		fmt.Fprintf(buf, "\n    async def get_%s(\n        self,\n", tbl.Name)
		for _, col := range pkCols {
			pyType := columnPythonType(col)
			fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
		}
		fmt.Fprintf(buf, "    ) -> Optional[%s]:\n", rowType)
		fmt.Fprintf(buf, "        return await self.%s.get_%s(%s)\n",
			attrName, tbl.Name, joinColumnNames(pkCols))
	}

	// update forwarding (skip if append-only).
	if !tbl.AppendOnly && len(pkCols) > 0 {
		var updatableCols []model.Column
		for _, col := range tbl.Columns {
			if isPKColumn(col.Name, tbl.PK) {
				continue
			}
			if col.Generated != "" {
				continue
			}
			if col.Identity != "" {
				continue
			}
			updatableCols = append(updatableCols, col)
		}

		fmt.Fprintf(buf, "\n    async def update_%s(\n        self,\n", tbl.Name)
		for _, col := range pkCols {
			pyType := columnPythonType(col)
			fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
		}
		buf.WriteString("        *,\n")
		for _, col := range updatableCols {
			pyType := baseColumnPythonType(col)
			optType := ApplyNullable(pyType, LangPython)
			fmt.Fprintf(buf, "        %s: %s = None,\n", col.Name, optType)
		}
		buf.WriteString("    ) -> None:\n")

		var kwArgs []string
		for _, col := range updatableCols {
			kwArgs = append(kwArgs, fmt.Sprintf("%s=%s", col.Name, col.Name))
		}
		fmt.Fprintf(buf, "        return await self.%s.update_%s(%s, %s)\n",
			attrName, tbl.Name, joinColumnNames(pkCols), strings.Join(kwArgs, ", "))
	}

	// delete forwarding (skip if append-only).
	if !tbl.AppendOnly && len(pkCols) > 0 {
		fmt.Fprintf(buf, "\n    async def delete_%s(\n        self,\n", tbl.Name)
		for _, col := range pkCols {
			pyType := columnPythonType(col)
			fmt.Fprintf(buf, "        %s: %s,\n", col.Name, pyType)
		}
		buf.WriteString("    ) -> bool:\n")
		fmt.Fprintf(buf, "        return await self.%s.delete_%s(%s)\n",
			attrName, tbl.Name, joinColumnNames(pkCols))
	}

	// SM transition forwarding.
	if len(pkCols) > 0 {
		for _, col := range tbl.Columns {
			smt, ok := smMap[col.SemanticTypeName]
			if !ok {
				continue
			}
			for _, nt := range smt.NamedTransitions {
				fmt.Fprintf(buf, "\n    async def %s_%s(\n        self,\n", nt.Name, tbl.Name)
				for _, pkCol := range pkCols {
					pyType := columnPythonType(pkCol)
					fmt.Fprintf(buf, "        %s: %s,\n", pkCol.Name, pyType)
				}
				if len(nt.Requires) > 0 {
					buf.WriteString("        *,\n")
					for _, paramName := range sortedMapKeys(nt.Requires) {
						pgType := nt.Requires[paramName]
						m := LookupType(pgType, LangPython)
						fmt.Fprintf(buf, "        %s: %s,\n", paramName, m.Type)
					}
				}
				buf.WriteString("    ) -> None:\n")

				var args []string
				args = append(args, joinColumnNames(pkCols))
				if len(nt.Requires) > 0 {
					for _, paramName := range sortedMapKeys(nt.Requires) {
						args = append(args, fmt.Sprintf("%s=%s", paramName, paramName))
					}
				}
				fmt.Fprintf(buf, "        return await self.%s.%s_%s(%s)\n",
					attrName, nt.Name, tbl.Name, strings.Join(args, ", "))
			}
		}
	}

	// get_by_fk forwarding.
	if schema.FKGraph != nil {
		type fkByMethod struct {
			colName string
			pyType  string
		}
		seen := make(map[string]bool)
		var fkMethods []fkByMethod

		for _, edge := range schema.FKGraph.Reverse[tbl.Name] {
			methodName := edge.FromColumn
			if seen[methodName] {
				continue
			}
			seen[methodName] = true

			pyType := "Any"
			if toCol := qlFindColumn(tbl, edge.ToColumn); toCol != nil {
				pyType = columnPythonType(*toCol)
			}
			fkMethods = append(fkMethods, fkByMethod{colName: methodName, pyType: pyType})
		}

		sort.Slice(fkMethods, func(i, j int) bool {
			return fkMethods[i].colName < fkMethods[j].colName
		})

		for _, fm := range fkMethods {
			fmt.Fprintf(buf, "\n    async def get_%s_by_%s(\n        self,\n        %s: %s,\n    ) -> list[%s]:\n",
				tbl.Name, fm.colName, fm.colName, fm.pyType, rowType)
			fmt.Fprintf(buf, "        return await self.%s.get_%s_by_%s(%s)\n",
				attrName, tbl.Name, fm.colName, fm.colName)
		}
	}

	// get_by_unique forwarding.
	for _, uq := range tbl.Uniques {
		suffix := strings.Join(uq.Columns, "_")
		fmt.Fprintf(buf, "\n    async def get_%s_by_%s(\n        self,\n", tbl.Name, suffix)
		for _, colName := range uq.Columns {
			col := qlFindColumn(tbl, colName)
			if col != nil {
				pyType := columnPythonType(*col)
				fmt.Fprintf(buf, "        %s: %s,\n", colName, pyType)
			} else {
				fmt.Fprintf(buf, "        %s: Any,\n", colName)
			}
		}
		fmt.Fprintf(buf, "    ) -> Optional[%s]:\n", rowType)
		fmt.Fprintf(buf, "        return await self.%s.get_%s_by_%s(%s)\n",
			attrName, tbl.Name, suffix, strings.Join(uq.Columns, ", "))
	}

	// list forwarding.
	fmt.Fprintf(buf, "\n    async def list_%s(\n        self,\n        limit: int = 100,\n        offset: int = 0,\n    ) -> list[%s]:\n",
		tbl.Name, rowType)
	fmt.Fprintf(buf, "        return await self.%s.list_%s(limit, offset)\n",
		attrName, tbl.Name)
}

// joinColumnNames joins column names with ", " for use as function arguments.
func joinColumnNames(cols []model.Column) string {
	names := make([]string, len(cols))
	for i, col := range cols {
		names[i] = col.Name
	}
	return strings.Join(names, ", ")
}
