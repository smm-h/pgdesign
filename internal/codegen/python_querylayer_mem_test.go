// extractMemMethodBody extracts from "async def <name>(" to the next method or class end.
func extractMemMethodBody(content, methodName string) string {
	marker := "async def " + methodName + "("
	idx := strings.Index(content, marker)
	if idx < 0 {
		return ""
	}
	rest := content[idx:]
	// Find the next method definition at the same indent level.
	nextIdx := strings.Index(rest[1:], "\n    async def ")
	if nextIdx < 0 {
		return rest
	}
	return rest[:nextIdx+1]
}
