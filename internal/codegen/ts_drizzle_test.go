func TestTSDrizzleGenerator_Basic(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "users",
			Comment: "Application users",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.MustParse("uuid"), NotNull: true},
				{Name: "email", PGType: typeinfo.MustParse("text"), NotNull: true},
				{Name: "score", PGType: typeinfo.MustParse("bigint"), NotNull: true},
				{Name: "is_active", PGType: typeinfo.MustParse("boolean"), NotNull: true},
				{Name: "created_at", PGType: typeinfo.MustParse("timestamptz"), NotNull: true},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Header.
	if !strings.Contains(result, "// Code generated by pgdesign -- do not edit.") {
		t.Error("missing generated header comment")
	}

	// Import from drizzle-orm/pg-core.
	if !strings.Contains(result, `from "drizzle-orm/pg-core"`) {
		t.Error("missing drizzle-orm/pg-core import")
	}
	for _, builder := range []string{"pgTable", "uuid", "text", "bigint", "boolean", "timestamp"} {
		if !strings.Contains(result, builder) {
			t.Errorf("missing %s in pg-core import", builder)
		}
	}

	// Table export.
	if !strings.Contains(result, `export const users = pgTable("users", {`) {
		t.Error("missing users table export")
	}

	// Column definitions.
	if !strings.Contains(result, `id: uuid("id").primaryKey().notNull(),`) {
		t.Error("missing id column definition")
	}
	if !strings.Contains(result, `email: text("email").notNull(),`) {
		t.Error("missing email column definition")
	}
	if !strings.Contains(result, `score: bigint("score").notNull(),`) {
		t.Error("missing score column definition")
	}
	if !strings.Contains(result, `isActive: boolean("is_active").notNull(),`) {
		t.Error("missing isActive column definition")
	}
	if !strings.Contains(result, `createdAt: timestamp("created_at").notNull(),`) {
		t.Error("missing createdAt column definition")
	}
}

