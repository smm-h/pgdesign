func TestTSDrizzleGenerator_TypeMapping(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "all_types",
			Comment: "All types",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.MustParse("integer"), NotNull: true},
				{Name: "big_id", PGType: typeinfo.MustParse("bigint"), NotNull: true},
				{Name: "small_id", PGType: typeinfo.MustParse("smallint"), NotNull: true},
				{Name: "name", PGType: typeinfo.MustParse("text"), NotNull: true},
				{Name: "active", PGType: typeinfo.MustParse("boolean"), NotNull: true},
				{Name: "uid", PGType: typeinfo.MustParse("uuid"), NotNull: true},
				{Name: "created_at", PGType: typeinfo.MustParse("timestamptz"), NotNull: true},
				{Name: "score", PGType: typeinfo.MustParse("real"), NotNull: true},
				{Name: "precise", PGType: typeinfo.MustParse("double precision"), NotNull: true},
				{Name: "amount", PGType: typeinfo.MustParse("numeric"), NotNull: true},
				{Name: "data", PGType: typeinfo.MustParse("jsonb"), NotNull: true},
				{Name: "raw_data", PGType: typeinfo.MustParse("bytea"), NotNull: true},
				{Name: "enum_col", PGType: typeinfo.MustParse("my_enum"), NotNull: true},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	checks := []struct {
		contains string
		desc     string
	}{
		{`id: integer("id")`, "integer mapping"},
		{`bigId: bigint("big_id")`, "bigint mapping"},
		{`smallId: smallint("small_id")`, "smallint mapping"},
		{`name: text("name")`, "text mapping"},
		{`active: boolean("active")`, "boolean mapping"},
		{`uid: uuid("uid")`, "uuid mapping"},
		{`createdAt: timestamp("created_at")`, "timestamp mapping"},
		{`score: real("score")`, "real mapping"},
		{`precise: doublePrecision("precise")`, "double precision mapping"},
		{`amount: numeric("amount")`, "numeric mapping"},
		{`data: jsonb("data")`, "jsonb mapping"},
	}

	for _, c := range checks {
		if !strings.Contains(result, c.contains) {
			t.Errorf("missing %s: expected %q", c.desc, c.contains)
		}
	}

	// bytea falls back to text with // bytea comment.
	found := false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, `rawData: text("raw_data")`) && strings.Contains(line, "// bytea") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing rawData text fallback with // bytea comment")
	}

	// Unknown enum type falls back to text with // my_enum comment.
	found = false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, `enumCol: text("enum_col")`) && strings.Contains(line, "// my_enum") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing enumCol text fallback with // my_enum comment")
	}
}

func TestTSDrizzleGenerator_Enums(t *testing.T) {
	schema := &model.Schema{
		Enums: []model.Enum{
			{Name: "user_role", Values: []string{"admin", "editor", "viewer"}},
			{Name: "account_status", Values: []string{"active", "inactive"}},
		},
		Tables: []model.Table{{
			Name:    "users",
			Comment: "Users",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: typeinfo.MustParse("integer"), NotNull: true},
				{Name: "role", PGType: typeinfo.MustParse("user_role"), NotNull: true, TypeKind: "enum"},
				{Name: "status", PGType: typeinfo.MustParse("account_status"), NotNull: true, TypeKind: "enum"},
				{Name: "name", PGType: typeinfo.MustParse("text"), NotNull: true},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// pgEnum declarations, sorted by name, before table definitions.
	if !strings.Contains(result, `export const accountStatusEnum = pgEnum("account_status", ["active", "inactive"]);`) {
		t.Error("missing accountStatusEnum declaration")
	}
	if !strings.Contains(result, `export const userRoleEnum = pgEnum("user_role", ["admin", "editor", "viewer"]);`) {
		t.Error("missing userRoleEnum declaration")
	}
	declIdx := strings.Index(result, "export const accountStatusEnum")
	sortedIdx := strings.Index(result, "export const userRoleEnum")
	tableIdx := strings.Index(result, "export const users = pgTable")
	if declIdx == -1 || sortedIdx == -1 || tableIdx == -1 {
		t.Fatal("missing expected declarations")
	}
	if !(declIdx < sortedIdx && sortedIdx < tableIdx) {
		t.Error("enum declarations must be sorted by name and precede table definitions")
	}

	// Enum columns use the local const as builder.
	if !strings.Contains(result, `role: userRoleEnum("role").notNull(),`) {
		t.Error("missing role column using userRoleEnum builder")
	}
	if !strings.Contains(result, `status: accountStatusEnum("status").notNull(),`) {
		t.Error("missing status column using accountStatusEnum builder")
	}

	// pgEnum is imported from pg-core; local const names are NOT.
	importLine := ""
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, `from "drizzle-orm/pg-core"`) {
			importLine = line
			break
		}
	}
	if importLine == "" {
		t.Fatal("missing pg-core import line")
	}
	if !strings.Contains(importLine, "pgEnum") {
		t.Error("missing pgEnum in pg-core imports")
	}
	if strings.Contains(importLine, "userRoleEnum") || strings.Contains(importLine, "accountStatusEnum") {
		t.Errorf("local enum const leaked into imports: %s", importLine)
	}

	// Non-enum column behavior unchanged.
	if !strings.Contains(result, `name: text("name").notNull(),`) {
		t.Error("missing name column with text builder")
	}
}

