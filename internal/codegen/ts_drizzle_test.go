func TestTSDrizzleGenerator_NullableAndArray(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "items",
			Comment: "Items",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: "integer", NotNull: true},
				{Name: "name", PGType: "text", NotNull: false},
				{Name: "tags", PGType: "text", NotNull: true, Array: true},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Nullable column should NOT have .notNull().
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, `name: text("name")`) && strings.Contains(line, ".notNull()") {
			t.Error("nullable name column should not have .notNull()")
		}
	}

	// Array column should have .notNull() and .array().
	found := false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, `tags: text("tags")`) &&
			strings.Contains(line, ".notNull()") &&
			strings.Contains(line, ".array()") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing tags column with .notNull().array()")
	}
}

func TestTSDrizzleGenerator_TypeMapping(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "all_types",
			Comment: "All types",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: "integer", NotNull: true},
				{Name: "big_id", PGType: "bigint", NotNull: true},
				{Name: "small_id", PGType: "smallint", NotNull: true},
				{Name: "name", PGType: "text", NotNull: true},
				{Name: "active", PGType: "boolean", NotNull: true},
				{Name: "uid", PGType: "uuid", NotNull: true},
				{Name: "created_at", PGType: "timestamptz", NotNull: true},
				{Name: "score", PGType: "real", NotNull: true},
				{Name: "precise", PGType: "double precision", NotNull: true},
				{Name: "amount", PGType: "numeric", NotNull: true},
				{Name: "data", PGType: "jsonb", NotNull: true},
				{Name: "raw_data", PGType: "bytea", NotNull: true},
				{Name: "enum_col", PGType: "my_enum", NotNull: true},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	checks := []struct {
		contains string
		desc     string
	}{
		{`id: integer("id")`, "integer mapping"},
		{`bigId: bigint("big_id")`, "bigint mapping"},
		{`smallId: smallint("small_id")`, "smallint mapping"},
		{`name: text("name")`, "text mapping"},
		{`active: boolean("active")`, "boolean mapping"},
		{`uid: uuid("uid")`, "uuid mapping"},
		{`createdAt: timestamp("created_at")`, "timestamp mapping"},
		{`score: real("score")`, "real mapping"},
		{`precise: doublePrecision("precise")`, "double precision mapping"},
		{`amount: numeric("amount")`, "numeric mapping"},
		{`data: jsonb("data")`, "jsonb mapping"},
	}

	for _, c := range checks {
		if !strings.Contains(result, c.contains) {
			t.Errorf("missing %s: expected %q", c.desc, c.contains)
		}
	}

	// bytea falls back to text with // bytea comment.
	found := false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, `rawData: text("raw_data")`) && strings.Contains(line, "// bytea") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing rawData text fallback with // bytea comment")
	}

	// Unknown enum type falls back to text with // my_enum comment.
	found = false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, `enumCol: text("enum_col")`) && strings.Contains(line, "// my_enum") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing enumCol text fallback with // my_enum comment")
	}
}
