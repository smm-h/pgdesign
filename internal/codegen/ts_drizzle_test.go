func TestTSDrizzleGenerator_Basic(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "users",
			Comment: "Application users",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: "uuid", NotNull: true},
				{Name: "email", PGType: "text", NotNull: true},
				{Name: "score", PGType: "bigint", NotNull: true},
				{Name: "is_active", PGType: "boolean", NotNull: true},
				{Name: "created_at", PGType: "timestamptz", NotNull: true},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Header.
	if !strings.Contains(result, "// Code generated by pgdesign -- do not edit.") {
		t.Error("missing generated header comment")
	}

	// Import from drizzle-orm/pg-core.
	if !strings.Contains(result, `from "drizzle-orm/pg-core"`) {
		t.Error("missing drizzle-orm/pg-core import")
	}
	for _, builder := range []string{"pgTable", "uuid", "text", "bigint", "boolean", "timestamp"} {
		if !strings.Contains(result, builder) {
			t.Errorf("missing %s in pg-core import", builder)
		}
	}

	// Table export.
	if !strings.Contains(result, `export const users = pgTable("users", {`) {
		t.Error("missing users table export")
	}

	// Column definitions.
	if !strings.Contains(result, `id: uuid("id").primaryKey().notNull(),`) {
		t.Error("missing id column definition")
	}
	if !strings.Contains(result, `email: text("email").notNull(),`) {
		t.Error("missing email column definition")
	}
	if !strings.Contains(result, `score: bigint("score").notNull(),`) {
		t.Error("missing score column definition")
	}
	if !strings.Contains(result, `isActive: boolean("is_active").notNull(),`) {
		t.Error("missing isActive column definition")
	}
	if !strings.Contains(result, `createdAt: timestamp("created_at").notNull(),`) {
		t.Error("missing createdAt column definition")
	}
}

func TestTSDrizzleGenerator_Defaults(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "configs",
			Comment: "Configs",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: "integer", NotNull: true},
				{Name: "enabled", PGType: "boolean", NotNull: true, Default: model.StrPtr("true")},
				{Name: "retries", PGType: "integer", NotNull: true, Default: model.StrPtr("3")},
				{Name: "created_at", PGType: "timestamptz", NotNull: true, Default: model.StrPtr("now()")},
				{Name: "label", PGType: "text", NotNull: true, Default: model.StrPtr("unnamed")},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Boolean default.
	found := false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, "enabled") && strings.Contains(line, ".default(true)") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing enabled .default(true)")
	}

	// Numeric default.
	found = false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, "retries") && strings.Contains(line, ".default(3)") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing retries .default(3)")
	}

	// Timestamp default.
	found = false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, "createdAt") && strings.Contains(line, ".defaultNow()") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing createdAt .defaultNow()")
	}

	// String default uses sql template.
	found = false
	for _, line := range strings.Split(result, "\n") {
		if strings.Contains(line, "label") && strings.Contains(line, ".default(sql`unnamed`)") {
			found = true
			break
		}
	}
	if !found {
		t.Error("missing label .default(sql`unnamed`)")
	}
	if !strings.Contains(result, `import { sql } from "drizzle-orm"`) {
		t.Error("missing sql import from drizzle-orm")
	}
}

func TestTSDrizzleGenerator_Relations(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "users",
				Comment: "Users",
				PK:      []string{"id"},
				Columns: []model.Column{
					{Name: "id", PGType: "uuid", NotNull: true},
					{Name: "name", PGType: "text", NotNull: true},
				},
			},
			{
				Name:    "orders",
				Comment: "Orders",
				PK:      []string{"id"},
				Columns: []model.Column{
					{Name: "id", PGType: "uuid", NotNull: true},
					{Name: "user_id", PGType: "uuid", NotNull: true},
					{Name: "total", PGType: "numeric", NotNull: true},
				},
				FKs: []model.FK{
					{Name: "fk_orders_user", Columns: []string{"user_id"}, RefTable: "users", RefColumns: []string{"id"}},
				},
			},
		},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Relations import.
	if !strings.Contains(result, `import { relations } from "drizzle-orm"`) {
		t.Error("missing relations import")
	}

	// Orders relations block with one().
	if !strings.Contains(result, "ordersRelations = relations(orders,") {
		t.Error("missing ordersRelations declaration")
	}
	if !strings.Contains(result, "{ one }") {
		t.Error("missing { one } destructure in ordersRelations")
	}
	if !strings.Contains(result, "users: one(users, {") {
		t.Error("missing users: one(users, {) in ordersRelations")
	}
	if !strings.Contains(result, "fields: [orders.userId],") {
		t.Error("missing fields: [orders.userId] in ordersRelations")
	}
	if !strings.Contains(result, "references: [users.id],") {
		t.Error("missing references: [users.id] in ordersRelations")
	}

	// Users relations block with many().
	if !strings.Contains(result, "usersRelations = relations(users,") {
		t.Error("missing usersRelations declaration")
	}
	if !strings.Contains(result, "{ many }") {
		t.Error("missing { many } destructure in usersRelations")
	}
	if !strings.Contains(result, "orders: many(orders),") {
		t.Error("missing orders: many(orders) in usersRelations")
	}
}

func TestTSDrizzleGenerator_CompositePK(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "order_items",
			Comment: "Order items",
			PK:      []string{"order_id", "item_id"},
			Columns: []model.Column{
				{Name: "order_id", PGType: "uuid", NotNull: true},
				{Name: "item_id", PGType: "uuid", NotNull: true},
				{Name: "quantity", PGType: "integer", NotNull: true},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Individual columns should NOT have .primaryKey().
	for _, line := range strings.Split(result, "\n") {
		trimmed := strings.TrimSpace(line)
		if (strings.Contains(trimmed, "orderId:") || strings.Contains(trimmed, "itemId:")) &&
			strings.Contains(trimmed, ".primaryKey()") {
			t.Errorf("composite PK column should not have .primaryKey(): %s", trimmed)
		}
	}

	// Third argument with composite PK.
	if !strings.Contains(result, "(table) => [") {
		t.Error("missing (table) => [ for composite PK")
	}
	if !strings.Contains(result, "primaryKey({ columns: [table.orderId, table.itemId] })") {
		t.Error("missing primaryKey({ columns: [...] }) declaration")
	}

	// Import should contain primaryKey.
	if !strings.Contains(result, "primaryKey") {
		t.Error("missing primaryKey in imports")
	}
}

func TestTSDrizzleGenerator_Indexes(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{{
			Name:    "events",
			Comment: "Events",
			PK:      []string{"id"},
			Columns: []model.Column{
				{Name: "id", PGType: "integer", NotNull: true},
				{Name: "email", PGType: "text", NotNull: true},
				{Name: "created_at", PGType: "timestamptz", NotNull: true},
			},
			Indexes: []model.Index{
				{Name: "idx_events_email", Columns: []string{"email"}, Unique: true},
				{Name: "idx_events_created_at", Columns: []string{"created_at"}, Unique: false},
			},
		}},
	}

	gen := &TSDrizzleGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Unique index.
	if !strings.Contains(result, `uniqueIndex("idx_events_email").on(table.email)`) {
		t.Error("missing uniqueIndex for email")
	}

	// Non-unique index.
	if !strings.Contains(result, `index("idx_events_created_at").on(table.createdAt)`) {
		t.Error("missing index for created_at")
	}

	// Imports should contain both.
	if !strings.Contains(result, "uniqueIndex") {
		t.Error("missing uniqueIndex import")
	}
	if !strings.Contains(result, "index") {
		t.Error("missing index import")
	}
}

