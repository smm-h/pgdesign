package codegen

import (
	"bytes"
	"fmt"
	"strings"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// JavaConstraintsGenerator generates Java validation methods for table constraints.
type JavaConstraintsGenerator struct{}

// Generate produces a Java file with validate<TableName> static methods for each
// table that has extractable constraints (NOT NULL, enum, CHECK, JSON schema).
func (g *JavaConstraintsGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by pgdesign -- do not edit.\n")
	buf.WriteString("// Regenerate with: pgdesign codegen --lang java --mode constraints <schema-files>\n")
	if schema.Name != "" {
		fmt.Fprintf(&buf, "// Schema: %s\n", schema.Name)
	}

	needsPattern := false

	// Collect tables with constraints.
	type tableWork struct {
		table model.Table
		cs    ConstraintSet
	}
	var work []tableWork
	for _, tbl := range schema.Tables {
		cs := ExtractConstraints(tbl, *schema)
		if !cs.HasConstraints() {
			continue
		}
		work = append(work, tableWork{table: tbl, cs: cs})
		for _, expr := range cs.CheckExprs {
			if pat := classifyCheck(expr); pat != nil {
				if _, ok := pat.(*likePattern); ok {
					needsPattern = true
				}
			}
		}
	}

	if len(work) == 0 {
		buf.WriteString("\n// No tables with extractable constraints.\n")
		return buf.Bytes(), nil
	}

	// Write imports.
	buf.WriteString("\nimport java.util.ArrayList;\n")
	buf.WriteString("import java.util.List;\n")
	buf.WriteString("import java.util.Set;\n")
	if needsPattern {
		buf.WriteString("import java.util.regex.Pattern;\n")
	}

	// Write ValidationError class.
	buf.WriteString(`
/**
 * Describes a single constraint violation.
 */
public record ValidationError(String field, String message) {}
`)

	// Write Constraints class.
	buf.WriteString(`
public final class Constraints {
    private Constraints() {}
`)

	// Generate per-table validators.
	for _, tw := range work {
		writeJavaValidator(&buf, tw.table, tw.cs)
	}

	buf.WriteString("}\n")

	return buf.Bytes(), nil
}

func writeJavaValidator(buf *bytes.Buffer, table model.Table, cs ConstraintSet) {
	funcName := "validate" + toPascalCase(table.Name)
	typeName := toPascalCase(table.Name)

	fmt.Fprintf(buf, "\n    /** Validates constraint rules for the %s table. */\n", table.Name)
	fmt.Fprintf(buf, "    public static List<ValidationError> %s(%s row) {\n", funcName, typeName)
	buf.WriteString("        List<ValidationError> errors = new ArrayList<>();\n")

	// NOT NULL checks.
	for _, col := range cs.NotNullFields {
		javaField := toCamelCase(col)
		pgType := columnPGType(table, col)
		if isStringPGType(pgType) {
			fmt.Fprintf(buf, "        if (row.%s() == null || row.%s().isEmpty()) {\n", javaField, javaField)
		} else {
			fmt.Fprintf(buf, "        if (row.%s() == null) {\n", javaField)
		}
		fmt.Fprintf(buf, "            errors.add(new ValidationError(%q, \"must not be empty\"));\n", col)
		buf.WriteString("        }\n")
	}

	// Enum checks.
	for col, values := range cs.EnumFields {
		javaField := toCamelCase(col)
		quoted := make([]string, len(values))
		for i, v := range values {
			quoted[i] = fmt.Sprintf("%q", v)
		}
		fmt.Fprintf(buf, "        if (row.%s() != null && !Set.of(%s).contains(row.%s())) {\n",
			javaField, strings.Join(quoted, ", "), javaField)
		fmt.Fprintf(buf, "            errors.add(new ValidationError(%q, \"invalid value \\\"\" + row.%s() + \"\\\"\"));\n", col, javaField)
		buf.WriteString("        }\n")
	}

	// CHECK constraint checks.
	for col, expr := range cs.CheckExprs {
		javaField := toCamelCase(col)
		pat := classifyCheck(expr)
		if pat == nil {
			fmt.Fprintf(buf, "        // CHECK on %s: %s (unrecognized pattern, skipped)\n", col, expr)
			continue
		}
		writeJavaCheckPattern(buf, col, javaField, pat)
	}

	buf.WriteString("        return errors;\n")
	buf.WriteString("    }\n")
}

func writeJavaCheckPattern(buf *bytes.Buffer, col, javaField string, pat checkPattern) {
	switch p := pat.(type) {
	case *rangePattern:
		var lowOp, highOp string
		if p.LowIncl {
			lowOp = "<"
		} else {
			lowOp = "<="
		}
		if p.HighIncl {
			highOp = ">"
		} else {
			highOp = ">="
		}
		fmt.Fprintf(buf, "        if (row.%s() != null && (row.%s() %s %s || row.%s() %s %s)) {\n",
			javaField, javaField, lowOp, p.Low, javaField, highOp, p.High)
		fmt.Fprintf(buf, "            errors.add(new ValidationError(%q, \"value out of range\"));\n", col)
		buf.WriteString("        }\n")

	case *comparisonPattern:
		failOp := invertComparisonOp(p.Op)
		fmt.Fprintf(buf, "        if (row.%s() != null && row.%s() %s %s) {\n", javaField, javaField, failOp, p.Value)
		fmt.Fprintf(buf, "            errors.add(new ValidationError(%q, \"failed check: %s %s\"));\n", col, p.Op, p.Value)
		buf.WriteString("        }\n")

	case *lengthPattern:
		failOp := invertComparisonOp(p.Op)
		fmt.Fprintf(buf, "        if (row.%s() != null && row.%s().length() %s %d) {\n", javaField, javaField, failOp, p.Value)
		fmt.Fprintf(buf, "            errors.add(new ValidationError(%q, String.format(\"length must be %s %d, got %%d\", row.%s().length())));\n", col, p.Op, p.Value, javaField)
		buf.WriteString("        }\n")

	case *likePattern:
		regex := likeToRegex(p.Pattern)
		if strings.HasPrefix(strings.ToUpper(p.Op), "NOT") {
			fmt.Fprintf(buf, "        if (row.%s() != null && Pattern.matches(%q, row.%s())) {\n", javaField, regex, javaField)
		} else {
			fmt.Fprintf(buf, "        if (row.%s() != null && !Pattern.matches(%q, row.%s())) {\n", javaField, regex, javaField)
		}
		fmt.Fprintf(buf, "            errors.add(new ValidationError(%q, \"does not match required pattern\"));\n", col)
		buf.WriteString("        }\n")
	}
}
