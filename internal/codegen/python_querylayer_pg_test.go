// --- Test Helpers ---

// extractPgMethodParams extracts the parameter section of a PG delegate method
// (from "async def <name>(" through the closing ") ->").
func extractPgMethodParams(content, methodName string) string {
	marker := "async def " + methodName + "("
	idx := strings.Index(content, marker)
	if idx < 0 {
		return ""
	}
	rest := content[idx:]
	endIdx := strings.Index(rest, ") ->")
	if endIdx < 0 {
		return rest
	}
	return rest[:endIdx]
}
