func TestPythonQueryLayerPg_ParameterizedSQL(t *testing.T) {
	schema := qlTestSchema()
	gen := &PythonQueryLayerGenerator{}
	files, _ := gen.GenerateFiles(schema)

	// Verify all per-table files use parameterized queries, no string interpolation
	// in SQL literals (except f-strings for dynamic SET clause which is safe).
	for name, data := range files {
		if !strings.HasPrefix(name, "_") || !strings.HasSuffix(name, "_pg.py") {
			continue
		}
		content := string(data)

		// Must have at least one $1 (parameterized query).
		if !strings.Contains(content, "$1") {
			t.Errorf("%s: no parameterized queries found (missing $1)", name)
		}

		// SQL string literals should not contain format-style interpolation.
		// Check for %s, %d patterns inside SQL strings (not in f-strings for SET).
		for _, line := range strings.Split(content, "\n") {
			trimmed := strings.TrimSpace(line)
			// Skip the f-string for dynamic UPDATE SET clause (safe pattern).
			if strings.Contains(trimmed, "f\"UPDATE") {
				continue
			}
			// Check for dangerous string interpolation in SQL.
			if strings.Contains(trimmed, "sql = \"") || strings.Contains(trimmed, "sql = f\"") {
				if strings.Contains(trimmed, "%s") || strings.Contains(trimmed, "%d") {
					t.Errorf("%s: possible string interpolation in SQL: %s", name, trimmed)
				}
			}
		}
	}
}

