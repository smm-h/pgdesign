func TestPythonQueryLayerPg_ForwardingMatchesProtocol(t *testing.T) {
	schema := qlTestSchema()
	gen := &PythonQueryLayerGenerator{}
	files, _ := gen.GenerateFiles(schema)

	protocols := string(files["protocols.py"])
	backend := string(files["pg_backend.py"])

	// Extract all method names from Backend protocol.
	backendSection := extractAfter(protocols, "class Backend(Protocol):")
	var protocolMethods []string
	for _, line := range strings.Split(backendSection, "\n") {
		trimmed := strings.TrimSpace(line)
		if strings.HasPrefix(trimmed, "async def ") {
			methodName := strings.TrimPrefix(trimmed, "async def ")
			if idx := strings.Index(methodName, "("); idx >= 0 {
				methodName = methodName[:idx]
			}
			protocolMethods = append(protocolMethods, methodName)
		}
	}

	if len(protocolMethods) == 0 {
		t.Fatal("could not extract any protocol methods")
	}

	// Verify every protocol method exists on PgBackend.
	for _, method := range protocolMethods {
		if !strings.Contains(backend, "async def "+method+"(") {
			t.Errorf("PgBackend missing protocol method: %s", method)
		}
	}
}

