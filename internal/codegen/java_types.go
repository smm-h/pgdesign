package codegen

import (
	"bytes"
	"fmt"
	"sort"
	"strings"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// JavaTypesGenerator generates Java record types corresponding to database tables.
type JavaTypesGenerator struct{}

// Generate produces a Java source file with one record per table in the schema.
func (g *JavaTypesGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	header := "// Code generated by pgdesign -- do not edit.\n"

	if len(schema.Tables) == 0 {
		buf.WriteString(header)
		return buf.Bytes(), nil
	}

	// First pass: collect type info and imports.
	type fieldInfo struct {
		Name     string
		JavaType string
	}
	type recordInfo struct {
		Name      string
		TableName string
		Fields    []fieldInfo
	}

	imports := make(map[string]bool)
	var records []recordInfo

	for _, tbl := range schema.Tables {
		ri := recordInfo{
			Name:      toPascalCase(tbl.Name),
			TableName: tbl.Name,
		}
		for _, col := range tbl.Columns {
			javaType, typeImports := pgTypeToJava(col)
			for _, imp := range typeImports {
				imports[imp] = true
			}
			ri.Fields = append(ri.Fields, fieldInfo{
				Name:     toCamelCase(col.Name),
				JavaType: javaType,
			})
		}
		records = append(records, ri)
	}

	// Separate stdlib and third-party imports.
	var stdlibImports, thirdPartyImports []string
	for imp := range imports {
		if strings.HasPrefix(imp, "java.") {
			stdlibImports = append(stdlibImports, imp)
		} else {
			thirdPartyImports = append(thirdPartyImports, imp)
		}
	}
	sort.Strings(stdlibImports)
	sort.Strings(thirdPartyImports)

	// Second pass: write output.
	buf.WriteString(header)

	if len(stdlibImports) > 0 || len(thirdPartyImports) > 0 {
		buf.WriteString("\n")
		for _, imp := range stdlibImports {
			fmt.Fprintf(&buf, "import %s;\n", imp)
		}
		if len(stdlibImports) > 0 && len(thirdPartyImports) > 0 {
			buf.WriteString("\n")
		}
		for _, imp := range thirdPartyImports {
			fmt.Fprintf(&buf, "import %s;\n", imp)
		}
	}

	for _, ri := range records {
		buf.WriteString("\n")
		fmt.Fprintf(&buf, "/** Represents the %s table. */\n", ri.TableName)
		fmt.Fprintf(&buf, "public record %s(\n", ri.Name)
		for i, f := range ri.Fields {
			if i < len(ri.Fields)-1 {
				fmt.Fprintf(&buf, "    %s %s,\n", f.JavaType, f.Name)
			} else {
				fmt.Fprintf(&buf, "    %s %s\n", f.JavaType, f.Name)
			}
		}
		buf.WriteString(") {}\n")
	}

	return buf.Bytes(), nil
}

// pgTypeToJava maps a column's PostgreSQL type to a Java type string and
// returns any import paths needed.
func pgTypeToJava(col model.Column) (string, []string) {
	baseType, baseImports := pgBaseTypeToJava(col)

	if col.Array {
		wrapperType, wrapperImports := toJavaWrapper(baseType, baseImports)
		allImports := append([]string{"java.util.List"}, wrapperImports...)
		return "List<" + wrapperType + ">", allImports
	}

	if !col.NotNull {
		wrapperType, wrapperImports := toJavaWrapper(baseType, baseImports)
		return wrapperType, wrapperImports
	}

	return baseType, baseImports
}

// pgBaseTypeToJava returns the primitive/reference Java type and imports for a
// column, ignoring array and nullable modifiers.
func pgBaseTypeToJava(col model.Column) (string, []string) {
	// Semantic type overrides.
	if col.SemanticTypeName == "money" {
		return "long", nil
	}

	pgType := strings.ToLower(col.PGType)

	switch pgType {
	case "text", "varchar", "character varying", "char", "character", "bpchar":
		return "String", nil
	case "integer", "int4":
		return "int", nil
	case "bigint", "int8":
		return "long", nil
	case "smallint", "int2":
		return "short", nil
	case "real", "float4":
		return "float", nil
	case "double precision", "float8":
		return "double", nil
	case "boolean", "bool":
		return "boolean", nil
	case "timestamptz", "timestamp", "timestamp with time zone", "timestamp without time zone":
		return "Instant", []string{"java.time.Instant"}
	case "date":
		return "Instant", []string{"java.time.Instant"}
	case "uuid":
		return "UUID", []string{"java.util.UUID"}
	case "jsonb", "json":
		return "JsonNode", []string{"com.fasterxml.jackson.databind.JsonNode"}
	case "numeric", "decimal":
		return "BigDecimal", []string{"java.math.BigDecimal"}
	case "bytea":
		return "byte[]", nil
	case "interval":
		return "String", nil
	default:
		// Enum types and anything unrecognized fall back to String.
		return "String", nil
	}
}

// toJavaWrapper converts a primitive Java type to its wrapper type for use in
// generics (List<T>) or nullable contexts. Reference types are returned as-is.
func toJavaWrapper(baseType string, imports []string) (string, []string) {
	switch baseType {
	case "int":
		return "Integer", imports
	case "long":
		return "Long", imports
	case "short":
		return "Short", imports
	case "float":
		return "Float", imports
	case "double":
		return "Double", imports
	case "boolean":
		return "Boolean", imports
	default:
		return baseType, imports
	}
}
