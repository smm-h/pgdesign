func TestJavaConstraintsGenerator_NoPatternWithoutLike(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name: "items",
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.MustParse("bigint"), NotNull: true},
					{Name: "price", PGType: typeinfo.MustParse("integer"), NotNull: true},
				},
				PK: []string{"id"},
				Checks: []model.CheckConstraint{
					{Name: "ck_items_price", Expr: "price >= 0"},
				},
			},
		},
	}

	gen := &JavaConstraintsGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)
	if strings.Contains(result, "java.util.regex.Pattern") {
		t.Error("should not import Pattern when no LIKE patterns exist")
	}
}
