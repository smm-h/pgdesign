package codegen

import (
	"bytes"
	"fmt"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// KotlinConstraintsGenerator generates Kotlin validation extension functions
// for table constraints. Types and constraints are emitted in the same file
// to avoid import issues.
type KotlinConstraintsGenerator struct{}

// Generate produces a Kotlin file with <TypeName>.validate() extension functions
// for each table that has extractable constraints (NOT NULL, enum, CHECK, JSON schema).
func (g *KotlinConstraintsGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	buf.WriteString("// Code generated by pgdesign -- do not edit.\n")
	buf.WriteString("// Regenerate with: pgdesign codegen --lang kotlin --mode constraints <schema-files>\n")
	if schema.Name != "" {
		fmt.Fprintf(&buf, "// Schema: %s\n", schema.Name)
	}

	needsRegex := false

	// Collect tables with constraints.
	type tableWork struct {
		table model.Table
		cs    ConstraintSet
	}
	var work []tableWork
	for _, tbl := range schema.Tables {
		cs := ExtractConstraints(tbl, *schema)
		if !cs.HasConstraints() {
			continue
		}
		work = append(work, tableWork{table: tbl, cs: cs})
		for _, expr := range cs.CheckExprs {
			if pat := classifyCheck(expr); pat != nil {
				if _, ok := pat.(*likePattern); ok {
					needsRegex = true
				}
			}
		}
	}

	if len(work) == 0 {
		buf.WriteString("\n// No tables with extractable constraints.\n")
		return buf.Bytes(), nil
	}

	// Write ValidationError data class.
	buf.WriteString(`
/**
 * Describes a single constraint violation.
 */
data class ValidationError(val field: String, val message: String)
`)

	// Emit data classes for tables that have constraints.
	for _, tw := range work {
		writeKotlinConstraintDataClass(&buf, tw.table)
	}

	// Emit validation extension functions.
	for _, tw := range work {
		writeKotlinValidator(&buf, tw.table, tw.cs, needsRegex)
	}

	return buf.Bytes(), nil
}

// writeKotlinConstraintDataClass emits a minimal data class for the table.
func writeKotlinConstraintDataClass(buf *bytes.Buffer, table model.Table) {
	typeName := toPascalCase(table.Name)
	fmt.Fprintf(buf, "\n/** Represents the %s table. */\n", table.Name)
	fmt.Fprintf(buf, "data class %s(\n", typeName)
	for i, col := range table.Columns {
		kotlinType, _ := pgTypeToKotlin(col)
		fieldName := toCamelCase(col.Name)
		if i < len(table.Columns)-1 {
			fmt.Fprintf(buf, "    val %s: %s,\n", fieldName, kotlinType)
		} else {
			fmt.Fprintf(buf, "    val %s: %s\n", fieldName, kotlinType)
		}
	}
	buf.WriteString(")\n")
}

func writeKotlinValidator(buf *bytes.Buffer, table model.Table, cs ConstraintSet, _ bool) {
	typeName := toPascalCase(table.Name)

	fmt.Fprintf(buf, "\n/** Validates constraint rules for the %s table. */\n", table.Name)
	fmt.Fprintf(buf, "fun %s.validate(): List<ValidationError> {\n", typeName)
	buf.WriteString("    val errors = mutableListOf<ValidationError>()\n")

	// NOT NULL checks (for nullable Kotlin fields).
	for _, col := range cs.NotNullFields {
		kotlinField := toCamelCase(col)
		fmt.Fprintf(buf, "    if (%s == null) {\n", kotlinField)
		fmt.Fprintf(buf, "        errors.add(ValidationError(field = %q, message = \"must not be empty\"))\n", col)
		buf.WriteString("    }\n")
	}

	// Enum checks.
	for _, ef := range cs.SortedEnumFields() {
		kotlinField := toCamelCase(ef.Column)
		fmt.Fprintf(buf, "    when (%s) {\n", kotlinField)
		for _, v := range ef.Values {
			fmt.Fprintf(buf, "        %q -> {}\n", v)
		}
		buf.WriteString("        null -> {}\n")
		fmt.Fprintf(buf, "        else -> errors.add(ValidationError(field = %q, message = \"invalid value \\\"$%s\\\"\"))\n", ef.Column, kotlinField)
		buf.WriteString("    }\n")
	}

	// CHECK constraint checks.
	for _, ce := range cs.SortedCheckExprs() {
		kotlinField := toCamelCase(ce.Column)
		pat := classifyCheck(ce.Expr)
		if pat == nil {
			fmt.Fprintf(buf, "    // CHECK on %s: %s (unrecognized pattern, skipped)\n", ce.Column, ce.Expr)
			continue
		}
		writeKotlinCheckPattern(buf, ce.Column, kotlinField, pat)
	}

	buf.WriteString("    return errors\n")
	buf.WriteString("}\n")
}

func writeKotlinCheckPattern(buf *bytes.Buffer, col, kotlinField string, pat checkPattern) {
	switch p := pat.(type) {
	case *rangePattern:
		var lowOp, highOp string
		if p.LowIncl {
			lowOp = "<"
		} else {
			lowOp = "<="
		}
		if p.HighIncl {
			highOp = ">"
		} else {
			highOp = ">="
		}
		fmt.Fprintf(buf, "    if (%s != null && (%s %s %s || %s %s %s)) {\n",
			kotlinField, kotlinField, lowOp, p.Low, kotlinField, highOp, p.High)
		fmt.Fprintf(buf, "        errors.add(ValidationError(field = %q, message = \"value out of range\"))\n", col)
		buf.WriteString("    }\n")

	case *comparisonPattern:
		failOp := invertComparisonOp(p.Op)
		fmt.Fprintf(buf, "    if (%s != null && %s %s %s) {\n", kotlinField, kotlinField, failOp, p.Value)
		fmt.Fprintf(buf, "        errors.add(ValidationError(field = %q, message = \"failed check: %s %s\"))\n", col, p.Op, p.Value)
		buf.WriteString("    }\n")

	case *lengthPattern:
		failOp := invertComparisonOp(p.Op)
		fmt.Fprintf(buf, "    if (%s != null && %s.length %s %d) {\n", kotlinField, kotlinField, failOp, p.Value)
		fmt.Fprintf(buf, "        errors.add(ValidationError(field = %q, message = \"length must be %s %d, got ${%s.length}\"))\n", col, p.Op, p.Value, kotlinField)
		buf.WriteString("    }\n")

	case *likePattern:
		regex := likeToRegex(p.Pattern)
		var regexExpr string
		if p.IsCaseInsensitive() {
			regexExpr = fmt.Sprintf("Regex(%q, RegexOption.IGNORE_CASE)", regex)
		} else {
			regexExpr = fmt.Sprintf("Regex(%q)", regex)
		}
		if p.IsNegated() {
			fmt.Fprintf(buf, "    if (%s != null && %s.matches(%s)) {\n", kotlinField, regexExpr, kotlinField)
		} else {
			fmt.Fprintf(buf, "    if (%s != null && !%s.matches(%s)) {\n", kotlinField, regexExpr, kotlinField)
		}
		fmt.Fprintf(buf, "        errors.add(ValidationError(field = %q, message = \"does not match required pattern\"))\n", col)
		buf.WriteString("    }\n")
	}
}
