package codegen

import (
	"bytes"
	"fmt"
	"strings"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// PythonTypesGenerator generates Python dataclass types corresponding to database tables.
type PythonTypesGenerator struct{}

// Generate produces a Python source file with one dataclass per table in the schema.
func (g *PythonTypesGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	header := "# Code generated by pgdesign -- do not edit.\n"

	if len(schema.Tables) == 0 {
		buf.WriteString(header)
		return buf.Bytes(), nil
	}

	// First pass: collect type info and determine needed imports.
	type fieldInfo struct {
		Name   string
		PyType string
	}
	type classInfo struct {
		Name      string
		TableName string
		Fields    []fieldInfo
	}

	var classes []classInfo
	imports := make(map[string]bool)

	for _, tbl := range schema.Tables {
		ci := classInfo{
			Name:      toPascalCase(tbl.Name),
			TableName: tbl.Name,
		}
		for _, col := range tbl.Columns {
			pyType, needed := pgTypeToPython(col)
			for _, imp := range needed {
				imports[imp] = true
			}
			ci.Fields = append(ci.Fields, fieldInfo{
				Name:   col.Name,
				PyType: pyType,
			})
		}
		classes = append(classes, ci)
	}

	// Write header.
	buf.WriteString(header)

	// Write imports.
	buf.WriteString("\nfrom __future__ import annotations\n")
	buf.WriteString("\nfrom dataclasses import dataclass\n")

	if imports["datetime"] {
		buf.WriteString("from datetime import datetime\n")
	}
	if imports["Decimal"] {
		buf.WriteString("from decimal import Decimal\n")
	}

	needAny := imports["Any"]
	needOptional := imports["Optional"]
	if needAny && needOptional {
		buf.WriteString("from typing import Any, Optional\n")
	} else if needAny {
		buf.WriteString("from typing import Any\n")
	} else if needOptional {
		buf.WriteString("from typing import Optional\n")
	}

	if imports["UUID"] {
		buf.WriteString("from uuid import UUID\n")
	}

	// Write classes.
	for _, ci := range classes {
		buf.WriteString("\n\n@dataclass\n")
		fmt.Fprintf(&buf, "class %s:\n", ci.Name)
		fmt.Fprintf(&buf, "    \"\"\"Represents the %s table.\"\"\"\n", ci.TableName)
		for _, f := range ci.Fields {
			fmt.Fprintf(&buf, "    %s: %s\n", f.Name, f.PyType)
		}
	}

	return buf.Bytes(), nil
}

// pgTypeToPython maps a column's PostgreSQL type to a Python type string and
// a list of import tokens that are needed. Import tokens are: "datetime",
// "Decimal", "Any", "Optional", "UUID".
func pgTypeToPython(col model.Column) (pyType string, needed []string) {
	var baseType string

	// Semantic type overrides.
	if col.SemanticTypeName == "money" {
		baseType = "int"
		return applyPythonModifiers(baseType, col, needed)
	}

	pgType := strings.ToLower(col.PGType)

	switch pgType {
	case "text", "varchar", "character varying", "char", "character", "bpchar":
		baseType = "str"
	case "integer", "int4", "bigint", "int8", "smallint", "int2":
		baseType = "int"
	case "real", "float4", "double precision", "float8":
		baseType = "float"
	case "boolean", "bool":
		baseType = "bool"
	case "timestamptz", "timestamp", "timestamp with time zone", "timestamp without time zone":
		baseType = "datetime"
		needed = append(needed, "datetime")
	case "date":
		baseType = "datetime"
		needed = append(needed, "datetime")
	case "uuid":
		baseType = "UUID"
		needed = append(needed, "UUID")
	case "jsonb", "json":
		baseType = "dict[str, Any]"
		needed = append(needed, "Any")
	case "numeric", "decimal":
		baseType = "Decimal"
		needed = append(needed, "Decimal")
	case "bytea":
		baseType = "bytes"
	case "interval":
		baseType = "str"
	default:
		// Enum types and anything unrecognized fall back to str.
		baseType = "str"
	}

	return applyPythonModifiers(baseType, col, needed)
}

// applyPythonModifiers wraps a base Python type with list[] and Optional[]
// based on the column's Array and NotNull flags.
func applyPythonModifiers(baseType string, col model.Column, needed []string) (string, []string) {
	if col.Array && !col.NotNull {
		needed = append(needed, "Optional")
		return "Optional[list[" + baseType + "]]", needed
	}
	if col.Array {
		return "list[" + baseType + "]", needed
	}
	if !col.NotNull {
		needed = append(needed, "Optional")
		return "Optional[" + baseType + "]", needed
	}
	return baseType, needed
}
