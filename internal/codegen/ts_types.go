package codegen

import (
	"bytes"
	"fmt"
	"strings"

	"github.com/smm-h/pgdesign/internal/diagnostic"
	"github.com/smm-h/pgdesign/internal/model"
)

// TSTypesGenerator generates TypeScript interface types corresponding to database tables.
type TSTypesGenerator struct{}

// Generate produces a TypeScript source file with one interface per table in the schema.
func (g *TSTypesGenerator) Generate(schema *model.Schema) ([]byte, []diagnostic.Diagnostic) {
	var buf bytes.Buffer
	header := "// Code generated by pgdesign -- do not edit.\n"

	if len(schema.Tables) == 0 {
		buf.WriteString(header)
		return buf.Bytes(), nil
	}

	// First pass: collect type info for all interfaces.
	type fieldInfo struct {
		Name   string
		TSType string
	}
	type interfaceInfo struct {
		Name      string
		TableName string
		Fields    []fieldInfo
	}

	var interfaces []interfaceInfo

	for _, tbl := range schema.Tables {
		ii := interfaceInfo{
			Name:      toPascalCase(tbl.Name),
			TableName: tbl.Name,
		}
		for _, col := range tbl.Columns {
			ii.Fields = append(ii.Fields, fieldInfo{
				Name:   toCamelCase(col.Name),
				TSType: pgTypeToTS(col),
			})
		}
		interfaces = append(interfaces, ii)
	}

	// Second pass: write output.
	buf.WriteString(header)

	// Write enum types for state machines (TS uses string literal unions).
	enumBlock := GenerateEnums(schema.Enums, LangTS)
	if enumBlock != "" {
		buf.WriteString("\n")
		buf.WriteString(enumBlock)
	}

	// Write state machine transition maps.
	smBlock := GenerateTransitionMaps(schema.StateMachineTransitions, LangTS)
	if smBlock != "" {
		buf.WriteString("\n")
		buf.WriteString(smBlock)
	}

	for i, ii := range interfaces {
		if i > 0 {
			buf.WriteString("\n")
		}
		buf.WriteString("\n")
		fmt.Fprintf(&buf, "/** Represents the %s table. */\n", ii.TableName)
		fmt.Fprintf(&buf, "export interface %s {\n", ii.Name)
		for _, f := range ii.Fields {
			fmt.Fprintf(&buf, "  %s: %s;\n", f.Name, f.TSType)
		}
		buf.WriteString("}\n")
	}

	return buf.Bytes(), nil
}

// toCamelCase converts a snake_case string to camelCase.
// For example: "id" -> "id", "is_active" -> "isActive", "created_at" -> "createdAt".
func toCamelCase(s string) string {
	pascal := toPascalCase(s)
	if len(pascal) == 0 {
		return pascal
	}
	return strings.ToLower(pascal[:1]) + pascal[1:]
}

// pgTypeToTS maps a column's PostgreSQL type to a TypeScript type string.
// It handles semantic types, arrays, and nullability.
func pgTypeToTS(col model.Column) string {
	var baseType string

	// Semantic type overrides.
	if col.SemanticTypeName == "money" {
		baseType = "number"
		return applyTSModifiers(baseType, col)
	}

	pgType := strings.ToLower(col.PGType)

	switch pgType {
	case "text", "varchar", "character varying", "char", "character", "bpchar":
		baseType = "string"
	case "integer", "int4", "bigint", "int8", "smallint", "int2", "real", "float4", "double precision", "float8":
		baseType = "number"
	case "boolean", "bool":
		baseType = "boolean"
	case "timestamptz", "timestamp", "timestamp with time zone", "timestamp without time zone":
		baseType = "Date"
	case "date":
		baseType = "Date"
	case "uuid":
		baseType = "string"
	case "jsonb", "json":
		baseType = "Record<string, unknown>"
	case "numeric", "decimal":
		baseType = "string"
	case "bytea":
		baseType = "Uint8Array"
	case "interval":
		baseType = "string"
	default:
		// Enum types and anything unrecognized fall back to string.
		baseType = "string"
	}

	return applyTSModifiers(baseType, col)
}

// applyTSModifiers appends TypeScript array and nullability suffixes to a base type.
func applyTSModifiers(baseType string, col model.Column) string {
	if col.Array && !col.NotNull {
		return baseType + "[] | null"
	}
	if col.Array {
		return baseType + "[]"
	}
	if !col.NotNull {
		return baseType + " | null"
	}
	return baseType
}
