// buildSMTypeMap creates a lookup from semantic type name to its SMTransitionMap.
func buildSMTypeMap(schema *model.Schema) map[string]*model.SMTransitionMap {
	m := make(map[string]*model.SMTransitionMap, len(schema.StateMachineTransitions))
	for i := range schema.StateMachineTransitions {
		smt := &schema.StateMachineTransitions[i]
		m[smt.TypeName] = smt
	}
	return m
}

// baseColumnPythonType returns the Python type for a column without nullable
// wrapping. Used for method parameters where Optional is applied separately.
func baseColumnPythonType(col model.Column) string {
	resolver := NewTypeResolver(LangPython)
	baseType := resolver.Resolve(col).Type
	if col.Array {
		baseType = ApplyArray(baseType, LangPython)
	}
	return baseType
}

// columnPythonType returns the Python type string for a column, applying
// nullable and array modifiers.
func columnPythonType(col model.Column) string {
	resolver := NewTypeResolver(LangPython)
	baseType := resolver.Resolve(col).Type

	if col.Array {
		baseType = ApplyArray(baseType, LangPython)
	}
	if !col.NotNull {
		baseType = ApplyNullable(baseType, LangPython)
	}
	return baseType
}

// pkColumns returns the Column objects for the table's primary key.
func pkColumns(tbl *model.Table) []model.Column {
	var result []model.Column
	for _, pkName := range tbl.PK {
		for _, col := range tbl.Columns {
			if col.Name == pkName {
				result = append(result, col)
				break
			}
		}
	}
	return result
}

// isPKColumn checks if a column name is part of the table's primary key.
func isPKColumn(name string, pk []string) bool {
	for _, p := range pk {
		if p == name {
			return true
		}
	}
	return false
}

// isAutoColumn returns true if the column is a PK column with Identity or
// DefaultExpr set (auto-generated values that should be excluded from create params).
func isAutoColumn(col model.Column, pk []string) bool {
	if !isPKColumn(col.Name, pk) {
		return false
	}
	return col.Identity != "" || col.DefaultExpr != ""
}

// pkReturnType returns the Python type for the table's PK. For single-column
// PKs it's the column type; for composite PKs it's a tuple.
func pkReturnType(tbl *model.Table) string {
	pkCols := pkColumns(tbl)
	if len(pkCols) == 1 {
		return columnPythonType(pkCols[0])
	}
	var parts []string
	for _, col := range pkCols {
		parts = append(parts, columnPythonType(col))
	}
	return "tuple[" + strings.Join(parts, ", ") + "]"
}

// qlFindColumn finds a column by name in a table. Separate from java_jpa's
// findColumn to accept a pointer receiver.
func qlFindColumn(tbl *model.Table, name string) *model.Column {
	for i := range tbl.Columns {
		if tbl.Columns[i].Name == name {
			return &tbl.Columns[i]
		}
	}
	return nil
}

// sortedMapKeys returns the keys of a map[string]string in sorted order.
func sortedMapKeys(m map[string]string) []string {
	keys := make([]string, 0, len(m))
	for k := range m {
		keys = append(keys, k)
	}
	sort.Strings(keys)
	return keys
}
