func TestPythonSQLAlchemyGenerator_ForeignKeys(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "users",
				Comment: "Users",
				Columns: []model.Column{
					{Name: "id", PGType: "uuid", NotNull: true},
				},
				PK: []string{"id"},
			},
			{
				Name:    "orders",
				Comment: "Orders",
				Columns: []model.Column{
					{Name: "id", PGType: "uuid", NotNull: true},
					{Name: "user_id", PGType: "uuid", NotNull: true},
				},
				PK: []string{"id"},
				FKs: []model.FK{
					{
						Name:       "fk_orders_user",
						Columns:    []string{"user_id"},
						RefTable:   "users",
						RefColumns: []string{"id"},
						OnDelete:   "CASCADE",
					},
				},
			},
		},
	}

	gen := &PythonSQLAlchemyGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// FK reference in the mapped_column.
	if !strings.Contains(result, `ForeignKey("users.id")`) {
		t.Error("missing ForeignKey reference in user_id column")
	}

	// ForeignKey in sqlalchemy imports.
	if !strings.Contains(result, "from sqlalchemy import") || !strings.Contains(result, "ForeignKey") {
		t.Error("missing ForeignKey in sqlalchemy imports")
	}

	// BelongsTo relationship on orders: user -> Users.
	if !containsSARel(result, "user", `"Users"`) {
		t.Error("missing belongs-to relationship on orders (user -> Users)")
	}

	// HasMany relationship on users: orders -> list[Orders].
	if !containsSARel(result, "orders", `list["Orders"]`) {
		t.Error("missing has-many relationship on users (orders -> list[Orders])")
	}

	// back_populates present.
	if !strings.Contains(result, "back_populates=") {
		t.Error("missing back_populates in relationships")
	}
}

