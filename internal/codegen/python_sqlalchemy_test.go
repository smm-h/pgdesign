func TestPythonSQLAlchemyGenerator_MultipleTables(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "orders",
				Comment: "Orders",
				Columns: []model.Column{
					{Name: "id", PGType: "uuid", NotNull: true},
					{Name: "total", PGType: "numeric", NotNull: true},
				},
			},
			{
				Name:    "order_items",
				Comment: "Order items",
				Columns: []model.Column{
					{Name: "id", PGType: "integer", NotNull: true},
					{Name: "quantity", PGType: "smallint", NotNull: true},
				},
			},
		},
	}

	gen := &PythonSQLAlchemyGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Both class declarations present.
	if !strings.Contains(result, "class Orders(Base):") {
		t.Error("missing Orders class declaration")
	}
	if !strings.Contains(result, "class OrderItems(Base):") {
		t.Error("missing OrderItems class declaration")
	}

	// Tablenames.
	if !strings.Contains(result, `__tablename__ = "orders"`) {
		t.Error("missing __tablename__ for orders")
	}
	if !strings.Contains(result, `__tablename__ = "order_items"`) {
		t.Error("missing __tablename__ for order_items")
	}

	// Spot-check a field from the second table.
	if !containsSAField(result, "quantity", "int") {
		t.Error("missing or incorrect quantity field (expected int)")
	}
}
