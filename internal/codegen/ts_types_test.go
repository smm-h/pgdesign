func TestTSTypesGenerator_MultipleTables(t *testing.T) {
	schema := &model.Schema{
		Tables: []model.Table{
			{
				Name:    "orders",
				Comment: "Orders",
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.MustParse("uuid"), NotNull: true},
					{Name: "total", PGType: typeinfo.MustParse("numeric"), NotNull: true},
				},
			},
			{
				Name:    "order_items",
				Comment: "Order items",
				Columns: []model.Column{
					{Name: "id", PGType: typeinfo.MustParse("integer"), NotNull: true},
					{Name: "quantity", PGType: typeinfo.MustParse("smallint"), NotNull: true},
				},
			},
		},
	}

	gen := &TSTypesGenerator{}
	out, diags := gen.Generate(schema)
	if len(diags) > 0 {
		t.Fatalf("unexpected diagnostics: %v", diags)
	}

	result := string(out)

	// Both interface declarations present.
	if !strings.Contains(result, "export interface Orders {") {
		t.Error("missing Orders interface declaration")
	}
	if !strings.Contains(result, "export interface OrderItems {") {
		t.Error("missing OrderItems interface declaration")
	}

	// Spot-check a field from the second table.
	if !containsTSField(result, "quantity", "number") {
		t.Error("missing or incorrect quantity field (expected number)")
	}
}
